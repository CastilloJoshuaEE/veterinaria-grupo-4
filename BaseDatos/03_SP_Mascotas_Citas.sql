CREATE OR ALTER PROCEDURE SP_BUSCAR_MASCOTAS
    @TERMINO VARCHAR(100)
AS
BEGIN
    SET NOCOUNT ON;
    SELECT M.ID_MASCOTA, M.ID_CLIENTE, M.NOMBRE, M.ESPECIE, M.RAZA, M.SEXO,
           M.FECHA_NACIMIENTO, M.PESO, M.COLOR, M.FOTO, M.FECHA_REGISTRO,
           C.NOMBRE + ' ' + C.APELLIDO AS NOMBRE_CLIENTE,
           C.CEDULA AS CEDULA_CLIENTE
    FROM MASCOTA M
    INNER JOIN CLIENTE C ON M.ID_CLIENTE = C.ID_CLIENTE
    WHERE M.NOMBRE LIKE '%' + @TERMINO + '%' 
       OR C.CEDULA LIKE '%' + @TERMINO + '%'
       OR C.NOMBRE LIKE '%' + @TERMINO + '%'
       OR C.APELLIDO LIKE '%' + @TERMINO + '%'
    ORDER BY M.NOMBRE;
END;
GO

