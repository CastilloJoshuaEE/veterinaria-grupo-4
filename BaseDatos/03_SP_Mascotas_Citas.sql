CREATE OR ALTER PROCEDURE SP_OBTENER_CITAS_PENDIENTES
AS
BEGIN
    SELECT C.ID_CITA,
           C.ESTADO,                                           
           C.FECHA_HORA,
           C.OBSERVACIONES,
           C.FECHA_REGISTRO,                                   

           -- Cliente
           CL.ID_CLIENTE,                                      
           CONCAT(CL.NOMBRE, ' ', CL.APELLIDO) AS NOMBRE_CLIENTE, 
           CL.CEDULA AS CEDULA_CLIENTE,

           -- Mascota
           M.ID_MASCOTA,                                       
           M.NOMBRE AS NOMBRE_MASCOTA,
           M.ESPECIE AS ESPECIE_MASCOTA,
           
           -- Servicio
           S.ID_SERVICIO,                                      
           S.NOMBRE_SERVICIO,

           -- Veterinario
           V.ID_VETERINARIO,
           V.NOMBRE  AS NOMBRE_VETERINARIO,
           V.APELLIDO AS APELLIDO_VETERINARIO,

           CONCAT(S.NOMBRE_SERVICIO, ' - ',
                  FORMAT(C.FECHA_HORA, 'dd/MM/yyyy HH:mm'),
                  ' - ', M.NOMBRE, ' (Dr. ', V.APELLIDO, ')') AS DISPLAY_TEXT
    FROM CITA C
    INNER JOIN CLIENTE     CL ON C.ID_CLIENTE     = CL.ID_CLIENTE
    INNER JOIN MASCOTA     M  ON C.ID_MASCOTA     = M.ID_MASCOTA
    INNER JOIN SERVICIO     S  ON C.ID_SERVICIO    = S.ID_SERVICIO
    INNER JOIN VETERINARIO V  ON C.ID_VETERINARIO = V.ID_VETERINARIO
    WHERE C.ESTADO = 'PENDIENTE'
    ORDER BY C.FECHA_HORA ASC;
END;
GO

CREATE OR ALTER PROCEDURE SP_VERIFICAR_CITA_PENDIENTE
    @ID_CITA INT
AS
BEGIN
    SET NOCOUNT ON;
    -- Si no existe ninguna atención médica para esta cita, sigue pendiente
    IF NOT EXISTS (SELECT 1 FROM ATENCION_MEDICA WHERE ID_CITA = @ID_CITA)
    BEGIN
        RETURN 1;  -- Sigue pendiente
    END
    RETURN 0;  -- Ya fue atendida
END;
GO

CREATE OR ALTER PROCEDURE SP_OBTENER_ATENCIONES_POR_CITA
    @ID_CITA INT
AS
BEGIN
    SET NOCOUNT ON;
    SELECT AM.ID_ATENCION_MEDICA, AM.FECHA, AM.DIAGNOSTICO,
           AM.TRATAMIENTO, AM.OBSERVACIONES,
           V.NOMBRE + ' ' + V.APELLIDO AS VETERINARIO -- Nombre del médico asignado a la cita
    FROM ATENCION_MEDICA AM
    INNER JOIN CITA C ON AM.ID_CITA = C.ID_CITA -- Puente necesario
    INNER JOIN VETERINARIO V ON C.ID_VETERINARIO = V.ID_VETERINARIO -- El médico viene de la CITA
    WHERE AM.ID_CITA = @ID_CITA
    ORDER BY AM.FECHA DESC;
END;
GO

CREATE OR ALTER TRIGGER TR_ACTUALIZAR_ESTADO_CITA
ON ATENCION_MEDICA
AFTER INSERT, UPDATE, DELETE
AS
BEGIN
    SET NOCOUNT ON;
    
    -- Recolectar IDs de las citas afectadas (ya sea por INSERT o DELETE)
    DECLARE @CitasAfectadas TABLE (ID_CITA INT);
    INSERT INTO @CitasAfectadas
    SELECT DISTINCT ID_CITA FROM inserted
    UNION
    SELECT DISTINCT ID_CITA FROM deleted;

    -- Actualizar el estado de la cita
    UPDATE C
    SET ESTADO = CASE
        -- Si hay un registro en atención médica para esta cita, se completó
        WHEN EXISTS (SELECT 1 FROM ATENCION_MEDICA AM WHERE AM.ID_CITA = C.ID_CITA) THEN 'REALIZADA'
        ELSE 'PENDIENTE'
    END
    FROM CITA C
    INNER JOIN @CitasAfectadas CA ON C.ID_CITA = CA.ID_CITA
    WHERE C.ESTADO <> 'CANCELADA'; -- Evitamos revivir citas que fueron canceladas manualmente
END;
