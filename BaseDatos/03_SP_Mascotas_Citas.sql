CREATE PROCEDURE SP_AGENDAR_CITA
    @ID_CLIENTE    INT,
    @ID_MASCOTA    INT,
    @ID_SERVICIO   INT,
    @ID_VETERINARIO INT,
    @FECHA_HORA    DATETIME,
    @OBSERVACIONES VARCHAR(255) = NULL
AS
BEGIN
    BEGIN TRY
        BEGIN TRANSACTION;

        IF EXISTS (
            SELECT 1 FROM CITA
            WHERE FECHA_HORA = @FECHA_HORA 
            AND ID_VETERINARIO = @ID_VETERINARIO
            AND ESTADO = 'PENDIENTE'
        )
        BEGIN
            ROLLBACK;
            SELECT -1 AS ID_CITA;  -- horario ocupado
            RETURN;
        END

        INSERT INTO CITA (ID_CLIENTE, ID_MASCOTA, ID_SERVICIO, ID_VETERINARIO, FECHA_HORA, OBSERVACIONES)
        VALUES (@ID_CLIENTE, @ID_MASCOTA, @ID_SERVICIO, @ID_VETERINARIO, @FECHA_HORA, @OBSERVACIONES);

        DECLARE @ID_CITA INT = SCOPE_IDENTITY();

        COMMIT;
        SELECT @ID_CITA AS ID_CITA;
    END TRY
    BEGIN CATCH
        ROLLBACK;
        SELECT -2 AS ID_CITA;  -- error general
    END CATCH
END;
GO

CREATE PROCEDURE SP_ACTUALIZAR_CITA_COMPLETA
    @ID_CITA       INT,
