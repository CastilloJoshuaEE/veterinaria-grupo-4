CREATE PROCEDURE SP_INSERTAR_MASCOTA
    @ID_CLIENTE      INT,
    @NOMBRE          VARCHAR(50),
    @ESPECIE         VARCHAR(30),
    @RAZA            VARCHAR(50)    = NULL,
    @SEXO            CHAR(1),
    @FECHA_NACIMIENTO DATE          = NULL,
    @PESO            DECIMAL(5,2)   = NULL,
    @COLOR           VARCHAR(30)    = NULL,
    @FOTO            VARBINARY(MAX) = NULL
AS
BEGIN
    INSERT INTO MASCOTA
        (ID_CLIENTE, NOMBRE, ESPECIE, RAZA, SEXO, FECHA_NACIMIENTO, PESO, COLOR, FOTO)
    VALUES
        (@ID_CLIENTE, @NOMBRE, @ESPECIE, @RAZA, @SEXO, @FECHA_NACIMIENTO, @PESO, @COLOR, @FOTO);
    SELECT SCOPE_IDENTITY() AS ID_MASCOTA;
END;
GO

CREATE PROCEDURE SP_ACTUALIZAR_MASCOTA
    @ID_MASCOTA      INT,
    @ID_CLIENTE      INT,
    @NOMBRE          VARCHAR(50),
    @ESPECIE         VARCHAR(30),
    @RAZA            VARCHAR(50)    = NULL,
    @SEXO            CHAR(1)        = NULL,
    @FECHA_NACIMIENTO DATE          = NULL,
    @PESO            DECIMAL(5,2)   = NULL,
    @COLOR           VARCHAR(30)    = NULL,
    @FOTO            VARBINARY(MAX) = NULL
AS
BEGIN
    BEGIN TRY
        UPDATE MASCOTA
        SET ID_CLIENTE       = @ID_CLIENTE,
            NOMBRE           = @NOMBRE,
            ESPECIE          = @ESPECIE,
            RAZA             = ISNULL(@RAZA, RAZA),
            SEXO             = ISNULL(@SEXO, SEXO),
            FECHA_NACIMIENTO = ISNULL(@FECHA_NACIMIENTO, FECHA_NACIMIENTO),
            PESO             = ISNULL(@PESO, PESO),
            COLOR            = ISNULL(@COLOR, COLOR),
            FOTO             = ISNULL(@FOTO, FOTO)
        WHERE ID_MASCOTA = @ID_MASCOTA;
        IF @@ROWCOUNT = 0 RETURN 0;
        RETURN 1;
    END TRY
    BEGIN CATCH
        THROW;
        RETURN 0;
    END CATCH
END;
GO
