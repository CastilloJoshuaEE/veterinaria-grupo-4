GO

CREATE PROCEDURE SP_CANCELAR_CITA
    @ID_CITA INT,
    @MOTIVO  VARCHAR(255) = NULL
AS
BEGIN
    BEGIN TRY
        BEGIN TRANSACTION;
        UPDATE CITA
        SET ESTADO = 'CANCELADA',
            OBSERVACIONES = ISNULL(OBSERVACIONES + ' | ', '') +
                            'Cancelación: ' + ISNULL(@MOTIVO, 'Sin motivo especificado')
        WHERE ID_CITA = @ID_CITA;
        
        IF @@ROWCOUNT > 0
        BEGIN
            COMMIT TRANSACTION;
            SELECT 1 AS RESULTADO;
        END
        ELSE
        BEGIN
            ROLLBACK TRANSACTION;
            SELECT 0 AS RESULTADO;
        END
    END TRY
    BEGIN CATCH
        ROLLBACK TRANSACTION;
        SELECT 0 AS RESULTADO;
    END CATCH
END;
GO

