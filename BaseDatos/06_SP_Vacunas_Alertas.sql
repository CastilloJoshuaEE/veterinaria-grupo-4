CREATE PROCEDURE SP_REGISTRAR_VACUNA_APLICADA
    @ID_MASCOTA      INT,
    @NOMBRE          VARCHAR(50),
    @DESCRIPCION     VARCHAR(255),
    @PERIODO_MESES   INT,
    @FECHA_APLICACION DATE
AS
BEGIN
    DECLARE @FECHA_PROXIMA DATE = DATEADD(MONTH, @PERIODO_MESES, @FECHA_APLICACION);
    INSERT INTO VACUNA_APLICADA
        (ID_MASCOTA, NOMBRE, DESCRIPCION, PERIODO_MESES, FECHA_APLICACION, FECHA_PROXIMA)
    VALUES
        (@ID_MASCOTA, @NOMBRE, @DESCRIPCION, @PERIODO_MESES, @FECHA_APLICACION, @FECHA_PROXIMA);
    SELECT SCOPE_IDENTITY() AS ID_VACUNA;
END;
GO

CREATE PROCEDURE SP_OBTENER_VACUNAS_APLICADAS_POR_MASCOTA
    @ID_MASCOTA INT
AS
BEGIN
    SELECT VA.ID_VACUNA, VA.NOMBRE, VA.DESCRIPCION, VA.FECHA_APLICACION, VA.FECHA_PROXIMA
    FROM VACUNA_APLICADA VA
    WHERE VA.ID_MASCOTA = @ID_MASCOTA
    ORDER BY VA.FECHA_APLICACION DESC;
END;
GO

CREATE PROCEDURE SP_OBTENER_HISTORIAL_VACUNACION
    @ID_MASCOTA INT
AS
BEGIN
    SELECT VA.ID_VACUNA, VA.NOMBRE, VA.DESCRIPCION, VA.FECHA_APLICACION, VA.FECHA_PROXIMA,
           CASE
               WHEN VA.FECHA_PROXIMA < GETDATE()                               THEN 'VENCIDA'
               WHEN DATEDIFF(DAY, GETDATE(), VA.FECHA_PROXIMA) <= 30           THEN 'POR VENCER'
               ELSE 'VIGENTE'
           END AS ESTADO,
           DATEDIFF(DAY, GETDATE(), VA.FECHA_PROXIMA) AS DIAS_RESTANTES
    FROM VACUNA_APLICADA VA
    WHERE VA.ID_MASCOTA = @ID_MASCOTA
    ORDER BY VA.FECHA_APLICACION DESC;
END;
GO

CREATE PROCEDURE SP_OBTENER_VACUNAS_PROXIMAS_A_VENCER
    @DIAS_ANTICIPACION INT = 30
AS
BEGIN
    SELECT VA.ID_VACUNA, M.ID_MASCOTA, M.NOMBRE AS NOMBRE_MASCOTA, M.ESPECIE,
           VA.NOMBRE AS NOMBRE_VACUNA, VA.FECHA_APLICACION, VA.FECHA_PROXIMA,
           DATEDIFF(DAY, GETDATE(), VA.FECHA_PROXIMA) AS DIAS_RESTANTES,
           C.ID_CLIENTE, C.NOMBRE + ' ' + C.APELLIDO AS NOMBRE_CLIENTE,
           C.TELEFONO, C.CORREO_ELECTRONICO
    FROM VACUNA_APLICADA VA
    INNER JOIN MASCOTA M ON VA.ID_MASCOTA = M.ID_MASCOTA
    INNER JOIN CLIENTE C ON M.ID_CLIENTE  = C.ID_CLIENTE
    WHERE VA.FECHA_PROXIMA BETWEEN GETDATE() AND DATEADD(DAY, @DIAS_ANTICIPACION, GETDATE())
    ORDER BY VA.FECHA_PROXIMA;
END;
GO

CREATE PROCEDURE SP_OBTENER_VACUNAS_PROXIMAS
AS
BEGIN
    SELECT VA.ID_VACUNA, VA.NOMBRE AS TIPO_VACUNA,
           M.NOMBRE AS MASCOTA,
           CONCAT(CL.NOMBRE, ' ', CL.APELLIDO) AS CLIENTE,
           CL.CEDULA AS CEDULA_CLIENTE,
           VA.FECHA_PROXIMA, VA.FECHA_APLICACION, VA.DESCRIPCION,
           CONCAT(VA.NOMBRE, ' - ', FORMAT(VA.FECHA_PROXIMA, 'dd/MM/yyyy'), ' - ', M.NOMBRE) AS DISPLAY_TEXT
    FROM VACUNA_APLICADA VA
    INNER JOIN MASCOTA M ON VA.ID_MASCOTA = M.ID_MASCOTA
    INNER JOIN CLIENTE CL ON M.ID_CLIENTE = CL.ID_CLIENTE
    WHERE VA.FECHA_PROXIMA >= GETDATE()
    ORDER BY VA.FECHA_PROXIMA;
END;
GO

