CREATE PROCEDURE SP_MARCAR_RECORDATORIO_LEIDO
    @ID_RECORDATORIO INT
AS
BEGIN
    BEGIN TRY
        BEGIN TRANSACTION;
        UPDATE RECORDATORIO_ENVIADO
        SET LEIDO = 1, CONTADOR_MOSTRADO = 0
        WHERE ID_RECORDATORIO = @ID_RECORDATORIO;
        IF @@ROWCOUNT > 0
        BEGIN COMMIT TRANSACTION; SELECT 1 AS RESULTADO; END
        ELSE
        BEGIN ROLLBACK TRANSACTION; SELECT 0 AS RESULTADO; END
    END TRY
    BEGIN CATCH
        ROLLBACK TRANSACTION;
        SELECT 0 AS RESULTADO;
    END CATCH
END;
GO

