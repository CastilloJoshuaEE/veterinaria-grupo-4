CREATE OR ALTER PROCEDURE SP_OBTENER_CITAS_POR_RANGO_FECHAS
    @FECHA_INICIO DATE,
    @FECHA_FIN    DATE
AS
BEGIN
    SELECT C.ID_CITA, C.ID_CLIENTE,
           CL.NOMBRE + ' ' + CL.APELLIDO AS NOMBRE_CLIENTE,
           C.ID_MASCOTA, M.NOMBRE AS NOMBRE_MASCOTA, -- Corregido de MC a C
           C.ID_SERVICIO, S.NOMBRE_SERVICIO,
           C.FECHA_HORA, C.ESTADO, C.OBSERVACIONES,
           CASE C.ESTADO
               WHEN 'PENDIENTE'  THEN 'Pendiente'
               WHEN 'REALIZADA'  THEN 'Realizada' -- Corregido de COMPLETADA
               WHEN 'CANCELADA'  THEN 'Cancelada'
           END AS ESTADO_DESCRIPCION,
           CASE WHEN S.NOMBRE_SERVICIO LIKE '%Vacuna%' THEN 'Vacunación' ELSE 'Consulta' END AS TIPO_CITA
    FROM CITA C
    INNER JOIN CLIENTE CL ON C.ID_CLIENTE = CL.ID_CLIENTE
    INNER JOIN MASCOTA M  ON C.ID_MASCOTA = M.ID_MASCOTA -- Alias 'M'
    INNER JOIN SERVICIO S  ON C.ID_SERVICIO = S.ID_SERVICIO
    WHERE CONVERT(DATE, C.FECHA_HORA) BETWEEN @FECHA_INICIO AND @FECHA_FIN
    ORDER BY C.FECHA_HORA;
END;
GO

CREATE PROCEDURE SP_VERIFICAR_RECORDATORIOS
    @ID_USUARIO INT = NULL
AS
BEGIN
    SET NOCOUNT ON;

    DECLARE @ID_CITA INT, @ID_VACUNA INT, @TIPO VARCHAR(20),
            @FECHA_EVENTO DATETIME, @MENSAJE_BASE VARCHAR(255), @MENSAJE_FINAL VARCHAR(500),
            @NOMBRE_CLIENTE VARCHAR(100), @NOMBRE_MASCOTA VARCHAR(50),
            @CEDULA_CLIENTE VARCHAR(10), @NOMBRE_SERVICIO VARCHAR(50),
            @VETERINARIOS NVARCHAR(MAX);

    DECLARE @Pendientes TABLE (
        ID INT IDENTITY(1,1), TIPO VARCHAR(20), ID_EVENTO INT,
        FECHA_EVENTO DATETIME, ANTICIPACION VARCHAR(20), MENSAJE_BASE VARCHAR(255),
        NOMBRE_CLIENTE VARCHAR(100), NOMBRE_MASCOTA VARCHAR(50),
        CEDULA_CLIENTE VARCHAR(10), NOMBRE_SERVICIO VARCHAR(50)
    );

    -- Citas pendientes
    INSERT INTO @Pendientes
        (TIPO, ID_EVENTO, FECHA_EVENTO, ANTICIPACION, MENSAJE_BASE,
         NOMBRE_CLIENTE, NOMBRE_MASCOTA, CEDULA_CLIENTE, NOMBRE_SERVICIO)
    SELECT 'CITA', C.ID_CITA, C.FECHA_HORA, RC.ANTICIPACION, RC.MENSAJE,
           CL.NOMBRE + ' ' + CL.APELLIDO, M.NOMBRE, CL.CEDULA, S.NOMBRE_SERVICIO
    FROM CITA C
    INNER JOIN CLIENTE      CL ON C.ID_CLIENTE  = CL.ID_CLIENTE
    INNER JOIN MASCOTA      M  ON C.ID_MASCOTA = M.ID_MASCOTA
    INNER JOIN SERVICIO     S  ON C.ID_SERVICIO = S.ID_SERVICIO
    CROSS JOIN RECORDATORIO_CONFIG RC
    WHERE C.ESTADO = 'PENDIENTE' AND RC.TIPO_RECORDATORIO = 'CITA' AND RC.ACTIVO = 1
      AND NOT EXISTS (
          SELECT 1 FROM RECORDATORIO_ENVIADO RE
          WHERE RE.ID_CITA = C.ID_CITA AND RE.TIPO = 'CITA'
            AND CAST(RE.FECHA_ENVIO AS DATE) = CAST(GETDATE() AS DATE)
      )
      AND (
          (RC.ANTICIPACION = '15_DIAS'     AND DATEDIFF(DAY,    GETDATE(), C.FECHA_HORA) = 15) OR
          (RC.ANTICIPACION = '7_DIAS'      AND DATEDIFF(DAY,    GETDATE(), C.FECHA_HORA) = 7)  OR
          (RC.ANTICIPACION = '1_MES'       AND DATEDIFF(MONTH,  GETDATE(), C.FECHA_HORA) = 1)  OR
          (RC.ANTICIPACION = '12_HORAS'    AND DATEDIFF(HOUR,   GETDATE(), C.FECHA_HORA) = 12) OR
          (RC.ANTICIPACION = '10_HORAS'    AND DATEDIFF(HOUR,   GETDATE(), C.FECHA_HORA) = 10) OR
          (RC.ANTICIPACION = '5_MINUTOS'   AND DATEDIFF(MINUTE, GETDATE(), C.FECHA_HORA) = 5)  OR
          (RC.ANTICIPACION = '30_SEGUNDOS' AND DATEDIFF(SECOND, GETDATE(), C.FECHA_HORA) = 30)
      );

    -- Vacunas próximas
    INSERT INTO @Pendientes
        (TIPO, ID_EVENTO, FECHA_EVENTO, ANTICIPACION, MENSAJE_BASE,
         NOMBRE_CLIENTE, NOMBRE_MASCOTA, CEDULA_CLIENTE, NOMBRE_SERVICIO)
    SELECT 'VACUNA', VA.ID_VACUNA, VA.FECHA_PROXIMA, RC.ANTICIPACION, RC.MENSAJE,
           CL.NOMBRE + ' ' + CL.APELLIDO, M.NOMBRE, CL.CEDULA, VA.NOMBRE
    FROM VACUNA_APLICADA VA
    INNER JOIN MASCOTA M ON VA.ID_MASCOTA = M.ID_MASCOTA
    INNER JOIN CLIENTE CL ON M.ID_CLIENTE = CL.ID_CLIENTE
    CROSS JOIN RECORDATORIO_CONFIG RC
    WHERE RC.TIPO_RECORDATORIO = 'VACUNA' AND RC.ACTIVO = 1
      AND NOT EXISTS (
          SELECT 1 FROM RECORDATORIO_ENVIADO RE
          WHERE RE.ID_VACUNA = VA.ID_VACUNA AND RE.TIPO = 'VACUNA'
            AND RE.MENSAJE LIKE RC.MENSAJE + '%'
            AND CAST(RE.FECHA_ENVIO AS DATE) = CAST(GETDATE() AS DATE)
      )
      AND (
          (RC.ANTICIPACION = '15_DIAS'     AND DATEDIFF(DAY,    GETDATE(), VA.FECHA_PROXIMA) = 15) OR
          (RC.ANTICIPACION = '7_DIAS'      AND DATEDIFF(DAY,    GETDATE(), VA.FECHA_PROXIMA) = 7)  OR
          (RC.ANTICIPACION = '1_MES'       AND DATEDIFF(MONTH,  GETDATE(), VA.FECHA_PROXIMA) = 1)  OR
          (RC.ANTICIPACION = '12_HORAS'    AND DATEDIFF(HOUR,   GETDATE(), VA.FECHA_PROXIMA) = 12) OR
          (RC.ANTICIPACION = '10_HORAS'    AND DATEDIFF(HOUR,   GETDATE(), VA.FECHA_PROXIMA) = 10) OR
          (RC.ANTICIPACION = '5_MINUTOS'   AND DATEDIFF(MINUTE, GETDATE(), VA.FECHA_PROXIMA) = 5)  OR
          (RC.ANTICIPACION = '30_SEGUNDOS' AND DATEDIFF(SECOND, GETDATE(), VA.FECHA_PROXIMA) = 30)
      );

    DECLARE @Total INT = (SELECT COUNT(*) FROM @Pendientes), @Idx INT = 1;

    WHILE @Idx <= @Total
    BEGIN
        SELECT @TIPO = TIPO,
               @ID_CITA    = CASE WHEN TIPO = 'CITA'   THEN ID_EVENTO ELSE NULL END,
               @ID_VACUNA  = CASE WHEN TIPO = 'VACUNA' THEN ID_EVENTO ELSE NULL END,
               @FECHA_EVENTO = FECHA_EVENTO, @MENSAJE_BASE = MENSAJE_BASE,
               @NOMBRE_CLIENTE = NOMBRE_CLIENTE, @NOMBRE_MASCOTA = NOMBRE_MASCOTA,
               @CEDULA_CLIENTE = CEDULA_CLIENTE, @NOMBRE_SERVICIO = NOMBRE_SERVICIO
        FROM @Pendientes WHERE ID = @Idx;

        SET @VETERINARIOS = '';
        IF @TIPO = 'CITA'
        BEGIN
            -- Obtenemos el nombre del veterinario específico de LA CITA
            SELECT @VETERINARIOS = V.NOMBRE + ' ' + V.APELLIDO
            FROM CITA C
            INNER JOIN VETERINARIO V ON C.ID_VETERINARIO = V.ID_VETERINARIO
            WHERE C.ID_CITA = @ID_CITA;

            SET @MENSAJE_FINAL = @MENSAJE_BASE + '. Detalles: ' +
                CONVERT(VARCHAR, @FECHA_EVENTO, 103) + ' ' + LEFT(CONVERT(VARCHAR, @FECHA_EVENTO, 108), 5) +
                ' - Servicio: ' + @NOMBRE_SERVICIO +
                '. Mascota: ' + @NOMBRE_MASCOTA +
                '. Veterinario: ' + ISNULL(@VETERINARIOS, 'Por asignar');
        END
        ELSE
        BEGIN
            SET @MENSAJE_FINAL = @MENSAJE_BASE + '. Detalles: ' +
                'Vacuna: ' + @NOMBRE_SERVICIO +
                ' - Próxima aplicación: ' + CONVERT(VARCHAR, @FECHA_EVENTO, 103) +
                ' - Mascota: ' + @NOMBRE_MASCOTA +
                ' - Cliente: ' + @NOMBRE_CLIENTE + ' (' + @CEDULA_CLIENTE + ')';
        END

        INSERT INTO RECORDATORIO_ENVIADO (ID_USUARIO, ID_CITA, ID_VACUNA, TIPO, MENSAJE)
        VALUES (@ID_USUARIO, @ID_CITA, @ID_VACUNA, @TIPO, @MENSAJE_FINAL);

        SET @Idx += 1;
    END

    IF @ID_USUARIO IS NOT NULL
        SELECT ID_RECORDATORIO, MENSAJE, FECHA_ENVIO, LEIDO
        FROM RECORDATORIO_ENVIADO
        WHERE ID_USUARIO = @ID_USUARIO AND LEIDO = 0
        ORDER BY FECHA_ENVIO DESC;
END;
GO

