CREATE PROCEDURE SP_REGISTRAR_RECORDATORIO
    @CORREO_USUARIO VARCHAR(100) = NULL,
    @ID_CITA        INT          = NULL,
    @ID_VACUNA      INT          = NULL,
    @TIPO           VARCHAR(20),
    @MENSAJE        VARCHAR(500),
    @ANTICIPACION   VARCHAR(20)  = NULL
AS
BEGIN
    DECLARE @ID_USUARIO INT = NULL;
    IF @CORREO_USUARIO IS NOT NULL
        SELECT @ID_USUARIO = ID_USUARIO FROM USUARIO WHERE CORREO_ELECTRONICO = @CORREO_USUARIO;

    INSERT INTO RECORDATORIO_ENVIADO
        (ID_USUARIO, ID_CITA, ID_VACUNA, TIPO, MENSAJE, FECHA_ENVIO)
    VALUES (
        @ID_USUARIO, @ID_CITA, @ID_VACUNA, @TIPO, @MENSAJE,
        CASE
            WHEN @ANTICIPACION = '15_DIAS' THEN DATEADD(DAY, -15,
                CASE WHEN @TIPO = 'CITA' THEN (SELECT FECHA_HORA    FROM CITA            WHERE ID_CITA   = @ID_CITA)
                     ELSE                     (SELECT FECHA_PROXIMA FROM VACUNA_APLICADA WHERE ID_VACUNA  = @ID_VACUNA) END)
            WHEN @ANTICIPACION = '7_DIAS'  THEN DATEADD(DAY, -7,
                CASE WHEN @TIPO = 'CITA' THEN (SELECT FECHA_HORA    FROM CITA            WHERE ID_CITA   = @ID_CITA)
                     ELSE                     (SELECT FECHA_PROXIMA FROM VACUNA_APLICADA WHERE ID_VACUNA  = @ID_VACUNA) END)
            WHEN @ANTICIPACION = '1_MES'   THEN DATEADD(MONTH, -1,
                CASE WHEN @TIPO = 'CITA' THEN (SELECT FECHA_HORA    FROM CITA            WHERE ID_CITA   = @ID_CITA)
                     ELSE                     (SELECT FECHA_PROXIMA FROM VACUNA_APLICADA WHERE ID_VACUNA  = @ID_VACUNA) END)
            ELSE GETDATE()
        END
    );
    SELECT SCOPE_IDENTITY() AS ID_RECORDATORIO;
END;
GO

CREATE PROCEDURE SP_ACTUALIZAR_RECORDATORIO
    @ID_RECORDATORIO INT,
    @MENSAJE         VARCHAR(500),
    @LEIDO           BIT = NULL
AS
BEGIN
    UPDATE RECORDATORIO_ENVIADO
    SET MENSAJE = @MENSAJE, LEIDO = ISNULL(@LEIDO, LEIDO)
    WHERE ID_RECORDATORIO = @ID_RECORDATORIO;
    SELECT @@ROWCOUNT AS FILAS_AFECTADAS;
END;
GO

CREATE PROCEDURE SP_ELIMINAR_RECORDATORIO
    @ID_RECORDATORIO INT
AS
BEGIN
    DELETE FROM RECORDATORIO_ENVIADO WHERE ID_RECORDATORIO = @ID_RECORDATORIO;
    SELECT @@ROWCOUNT AS FILAS_AFECTADAS;
END;
GO

