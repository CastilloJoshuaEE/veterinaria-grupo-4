CREATE PROCEDURE SP_OBTENER_INSTRUMENTOS_POR_ATENCION
    @ID_ATENCION_MEDICA INT
AS
BEGIN
    SET NOCOUNT ON;
    SELECT IM.NOMBRE, IM.COSTO_USO
    FROM INSTRUMENTO_USADO    IU
