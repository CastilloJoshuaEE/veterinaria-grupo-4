GO

CREATE PROCEDURE SP_INSERTAR_INSTRUMENTO_USADO
    @ID_ATENCION_MEDICA INT,
    @ID_INSTRUMENTO     INT
AS
BEGIN
    INSERT INTO INSTRUMENTO_USADO (ID_ATENCION_MEDICA, ID_INSTRUMENTO)
    VALUES (@ID_ATENCION_MEDICA, @ID_INSTRUMENTO);
END;
GO

CREATE PROCEDURE SP_OBTENER_INSTRUMENTOS_POR_ATENCION
    @ID_ATENCION_MEDICA INT
AS
BEGIN
    SET NOCOUNT ON;
    SELECT IM.NOMBRE, IM.COSTO_USO
    FROM INSTRUMENTO_USADO    IU
    INNER JOIN INSTRUMENTO_MEDICO IM ON IU.ID_INSTRUMENTO = IM.ID_INSTRUMENTO
    WHERE IU.ID_ATENCION_MEDICA = @ID_ATENCION_MEDICA;
END;
GO

CREATE PROCEDURE SP_OBTENER_ULTIMO_INSTRUMENTO_USADO
    @ID_ATENCION_MEDICA INT
AS
BEGIN
    SET NOCOUNT ON;
    SELECT TOP 1 ID_INSTRUMENTO_USADO
    FROM INSTRUMENTO_USADO
    WHERE ID_ATENCION_MEDICA = @ID_ATENCION_MEDICA
    ORDER BY ID_INSTRUMENTO_USADO DESC;
END;
GO

