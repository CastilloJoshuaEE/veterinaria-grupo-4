CREATE PROCEDURE SP_OBTENER_SERVICIOS
AS
BEGIN
    SELECT ID_SERVICIO, NOMBRE_SERVICIO, DESCRIPCION, PRECIO, DURACION_ESTIMADA, ESTADO
    FROM SERVICIO ORDER BY NOMBRE_SERVICIO;
END;
GO

CREATE PROCEDURE SP_OBTENER_SERVICIOS_ACTIVOS
AS
BEGIN
    SELECT ID_SERVICIO, NOMBRE_SERVICIO, DESCRIPCION, PRECIO, DURACION_ESTIMADA
    FROM SERVICIO WHERE ESTADO = 1 ORDER BY NOMBRE_SERVICIO;
END;
GO

CREATE PROCEDURE SP_OBTENER_SERVICIO_POR_ID
    @ID_SERVICIO INT
AS
BEGIN
    SELECT ID_SERVICIO, NOMBRE_SERVICIO, DESCRIPCION, PRECIO, DURACION_ESTIMADA, ESTADO
    FROM SERVICIO WHERE ID_SERVICIO = @ID_SERVICIO;
END;
GO

CREATE PROCEDURE SP_INSERTAR_SERVICIO
    @NOMBRE_SERVICIO  VARCHAR(50),
    @DESCRIPCION      VARCHAR(255),
    @PRECIO           DECIMAL(10,2),
    @DURACION_ESTIMADA INT,
    @ESTADO           BIT
AS
BEGIN
    IF NOT EXISTS (SELECT 1 FROM SERVICIO WHERE NOMBRE_SERVICIO = @NOMBRE_SERVICIO)
    BEGIN
        INSERT INTO SERVICIO (NOMBRE_SERVICIO, DESCRIPCION, PRECIO, DURACION_ESTIMADA, ESTADO)
        VALUES (@NOMBRE_SERVICIO, @DESCRIPCION, @PRECIO, @DURACION_ESTIMADA, @ESTADO);
        SELECT SCOPE_IDENTITY() AS ID_SERVICIO;
    END
    ELSE
        SELECT -1 AS ID_SERVICIO;  -- ya existe
END;
GO

CREATE PROCEDURE SP_ACTUALIZAR_SERVICIO
    @ID_SERVICIO       INT,
    @NOMBRE_SERVICIO   VARCHAR(50),
    @DESCRIPCION       VARCHAR(255),
    @PRECIO            DECIMAL(10,2),
    @DURACION_ESTIMADA INT,
    @ESTADO            BIT
AS
BEGIN
    BEGIN TRY
        IF NOT EXISTS (SELECT 1 FROM SERVICIO WHERE ID_SERVICIO = @ID_SERVICIO)
        BEGIN SELECT 0 AS FILAS_AFECTADAS; RETURN; END

        IF EXISTS (SELECT 1 FROM SERVICIO WHERE NOMBRE_SERVICIO = @NOMBRE_SERVICIO AND ID_SERVICIO <> @ID_SERVICIO)
        BEGIN SELECT -1 AS FILAS_AFECTADAS; RETURN; END  -- nombre en uso

        UPDATE SERVICIO
        SET NOMBRE_SERVICIO = @NOMBRE_SERVICIO, DESCRIPCION = @DESCRIPCION,
            PRECIO = @PRECIO, DURACION_ESTIMADA = @DURACION_ESTIMADA, ESTADO = @ESTADO
        WHERE ID_SERVICIO = @ID_SERVICIO;
        SELECT @@ROWCOUNT AS FILAS_AFECTADAS;
    END TRY
    BEGIN CATCH
        SELECT 0 AS FILAS_AFECTADAS;
    END CATCH
END;
GO

CREATE PROCEDURE SP_CAMBIAR_ESTADO_SERVICIO
    @ID_SERVICIO INT,
    @ESTADO      BIT
AS
BEGIN
    UPDATE SERVICIO SET ESTADO = @ESTADO WHERE ID_SERVICIO = @ID_SERVICIO;
    SELECT @@ROWCOUNT AS FILAS_AFECTADAS;
END;
GO

CREATE PROCEDURE SP_ELIMINAR_SERVICIO_COMPLETO
    @ID_SERVICIO INT
AS
BEGIN
    BEGIN TRY
        BEGIN TRANSACTION;
        DELETE FROM SERVICIO_VETERINARIO WHERE ID_SERVICIO = @ID_SERVICIO;
        DELETE FROM SERVICIO             WHERE ID_SERVICIO = @ID_SERVICIO;
        COMMIT TRANSACTION;
        SELECT 1 AS RESULTADO;
    END TRY
    BEGIN CATCH
        ROLLBACK TRANSACTION;
        SELECT 0 AS RESULTADO;
    END CATCH
END;
GO

CREATE PROCEDURE SP_ASIGNAR_VETERINARIO_SERVICIO
    @ID_SERVICIO    INT,
    @ID_VETERINARIO INT
AS
BEGIN
    IF NOT EXISTS (SELECT 1 FROM SERVICIO_VETERINARIO
                   WHERE ID_SERVICIO = @ID_SERVICIO AND ID_VETERINARIO = @ID_VETERINARIO)
    BEGIN
        INSERT INTO SERVICIO_VETERINARIO (ID_SERVICIO, ID_VETERINARIO)
        VALUES (@ID_SERVICIO, @ID_VETERINARIO);
        SELECT 1 AS RESULTADO;
    END
    ELSE
        SELECT 0 AS RESULTADO;
END;
GO

CREATE PROCEDURE SP_ASIGNAR_MULTIPLES_VETERINARIOS
    @ID_SERVICIO      INT,
    @IDS_VETERINARIOS VARCHAR(MAX)
AS
BEGIN
    DECLARE @TEMP TABLE (ID_VETERINARIO INT);
    INSERT INTO @TEMP SELECT value FROM STRING_SPLIT(@IDS_VETERINARIOS, ',');
    INSERT INTO SERVICIO_VETERINARIO (ID_SERVICIO, ID_VETERINARIO)
    SELECT @ID_SERVICIO, ID_VETERINARIO FROM @TEMP T
    WHERE NOT EXISTS (
        SELECT 1 FROM SERVICIO_VETERINARIO
        WHERE ID_SERVICIO = @ID_SERVICIO AND ID_VETERINARIO = T.ID_VETERINARIO
    );
    SELECT @@ROWCOUNT AS ASIGNACIONES_REALIZADAS;
END;
GO

CREATE PROCEDURE SP_ELIMINAR_ASIGNACION_VETERINARIO
    @ID_SERVICIO_VETERINARIO INT
AS
BEGIN
    DELETE FROM SERVICIO_VETERINARIO WHERE ID_SERVICIO_VETERINARIO = @ID_SERVICIO_VETERINARIO;
    SELECT @@ROWCOUNT AS FILAS_AFECTADAS;
END;
GO
CREATE OR ALTER PROCEDURE SP_OBTENER_VETERINARIOS_POR_SERVICIO
    @ID_SERVICIO INT
AS
BEGIN
    SELECT 
        V.ID_VETERINARIO, 
        V.CEDULA,  
        V.NOMBRE, 
        V.APELLIDO,  
        V.TELEFONO,
        V.ID_ESPECIALIDAD,
        E.NOMBRE_ESPECIALIDAD AS ESPECIALIDAD,
        SV.FECHA_ASIGNACION 
    FROM SERVICIO_VETERINARIO SV 
    INNER JOIN VETERINARIO V ON SV.ID_VETERINARIO = V.ID_VETERINARIO 
    LEFT JOIN ESPECIALIDAD_VETERINARIA E ON V.ID_ESPECIALIDAD = E.ID_ESPECIALIDAD 
    WHERE SV.ID_SERVICIO = @ID_SERVICIO 
    ORDER BY V.NOMBRE, V.APELLIDO;
END;
GO
CREATE OR ALTER PROCEDURE SP_OBTENER_VETERINARIOS_NO_ASIGNADOS
    @ID_SERVICIO INT
AS
BEGIN
    SELECT 
        V.ID_VETERINARIO, 
        V.CEDULA, 
        V.NOMBRE, 
        V.APELLIDO,
        V.TELEFONO,
        V.ID_ESPECIALIDAD,
        E.NOMBRE_ESPECIALIDAD AS ESPECIALIDAD 
    FROM VETERINARIO V 
    LEFT JOIN ESPECIALIDAD_VETERINARIA E ON V.ID_ESPECIALIDAD = E.ID_ESPECIALIDAD 
    WHERE V.ID_VETERINARIO NOT IN ( 
        SELECT ID_VETERINARIO  
        FROM SERVICIO_VETERINARIO  
        WHERE ID_SERVICIO = @ID_SERVICIO 
    ) 
    ORDER BY V.NOMBRE, V.APELLIDO;
END;
GO

CREATE PROCEDURE SP_OBTENER_SERVICIOS_POR_VETERINARIO
    @ID_VETERINARIO INT
AS
BEGIN
    SELECT SV.ID_SERVICIO_VETERINARIO, S.ID_SERVICIO, S.NOMBRE_SERVICIO
    FROM SERVICIO_VETERINARIO SV
    INNER JOIN SERVICIO S ON SV.ID_SERVICIO = S.ID_SERVICIO
    WHERE SV.ID_VETERINARIO = @ID_VETERINARIO
    ORDER BY S.NOMBRE_SERVICIO;
END;
GO
