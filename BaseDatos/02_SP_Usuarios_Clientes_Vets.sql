GO

CREATE OR ALTER PROCEDURE SP_OBTENER_VETERINARIOS
AS
BEGIN
    SELECT 
        V.ID_VETERINARIO, 
        V.CEDULA, 
        V.NOMBRE, 
        V.APELLIDO, 
        V.TELEFONO,
        V.PAGO_MENSUAL, 
        V.DIRECCION, 
        V.CORREO_ELECTRONICO, 
        V.FECHA_REGISTRO,
        V.ID_ESPECIALIDAD AS ID_ESPECIALIDAD, 
        E.NOMBRE_ESPECIALIDAD AS NOMBRE_ESPECIALIDAD
    FROM VETERINARIO V
    LEFT JOIN ESPECIALIDAD_VETERINARIA E ON V.ID_ESPECIALIDAD = E.ID_ESPECIALIDAD
    ORDER BY V.NOMBRE, V.APELLIDO;
END;
GO

CREATE OR ALTER PROCEDURE SP_BUSCAR_VETERINARIOS
    @TERMINO VARCHAR(100)
AS
BEGIN
    SET NOCOUNT ON;
    SELECT V.ID_VETERINARIO, 
           V.CEDULA, 
           V.NOMBRE, 
           V.APELLIDO, 
           V.TELEFONO,
           V.ID_ESPECIALIDAD,
           E.NOMBRE_ESPECIALIDAD AS NOMBRE_ESPECIALIDAD,
           V.CORREO_ELECTRONICO
    FROM VETERINARIO V
    LEFT JOIN ESPECIALIDAD_VETERINARIA E ON V.ID_ESPECIALIDAD = E.ID_ESPECIALIDAD
    WHERE V.NOMBRE   LIKE '%' + @TERMINO + '%' 
       OR V.APELLIDO LIKE '%' + @TERMINO + '%'
       OR V.CEDULA   LIKE '%' + @TERMINO + '%'
    ORDER BY V.NOMBRE ASC;
END;
GO

