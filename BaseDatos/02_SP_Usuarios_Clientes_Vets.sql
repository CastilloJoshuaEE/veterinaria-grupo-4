CREATE OR ALTER PROCEDURE SP_OBTENER_USUARIO
    @EMAIL VARCHAR(100)
AS
BEGIN
    SELECT 
        U.ID_USUARIO,
        U.CORREO_ELECTRONICO,
        U.ROL,
        U.FECHA_CREACION,
        U.ESTADO,
        -- Datos de la persona asociada
        CASE 
            WHEN EXISTS (SELECT 1 FROM VETERINARIO WHERE CORREO_ELECTRONICO = U.CORREO_ELECTRONICO) 
                THEN 'VETERINARIO'
            WHEN EXISTS (SELECT 1 FROM RECEPCIONISTA WHERE CORREO_ELECTRONICO = U.CORREO_ELECTRONICO) 
                THEN 'RECEPCIONISTA'
            ELSE U.ROL
        END AS ROL_EFECTIVO,
        CASE 
            WHEN EXISTS (SELECT 1 FROM VETERINARIO V WHERE V.CORREO_ELECTRONICO = U.CORREO_ELECTRONICO) 
                THEN (SELECT CONCAT(NOMBRE, ' ', APELLIDO) FROM VETERINARIO WHERE CORREO_ELECTRONICO = U.CORREO_ELECTRONICO)
            WHEN EXISTS (SELECT 1 FROM RECEPCIONISTA R WHERE R.CORREO_ELECTRONICO = U.CORREO_ELECTRONICO) 
                THEN (SELECT CONCAT(NOMBRE, ' ', APELLIDO) FROM RECEPCIONISTA WHERE CORREO_ELECTRONICO = U.CORREO_ELECTRONICO)
            ELSE 'Administrador'
        END AS NOMBRE_COMPLETO
    FROM USUARIO U
    WHERE U.CORREO_ELECTRONICO = @EMAIL;
END;
GO

