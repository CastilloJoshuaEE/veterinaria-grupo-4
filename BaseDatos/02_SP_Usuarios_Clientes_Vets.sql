CREATE PROCEDURE SP_VALIDAR_USUARIO
    @NOMBRE_USUARIO VARCHAR(50),
    @CONTRASENA     VARCHAR(100)
AS
BEGIN
    SELECT ID_USUARIO, NOMBRE_USUARIO, ROL
    FROM USUARIO
    WHERE NOMBRE_USUARIO = @NOMBRE_USUARIO
      AND CONTRASENA     = @CONTRASENA
      AND ESTADO         = 1;
END;
GO

CREATE PROCEDURE SP_INSERTAR_USUARIO
    @NOMBRE_USUARIO    VARCHAR(50),
    @CONTRASENA        VARCHAR(100),
    @CORREO_ELECTRONICO VARCHAR(100),
    @ROL               VARCHAR(20) = 'ADMINISTRADOR'
AS
BEGIN
    IF NOT EXISTS (SELECT 1 FROM USUARIO WHERE NOMBRE_USUARIO = @NOMBRE_USUARIO)
    BEGIN
        INSERT INTO USUARIO (NOMBRE_USUARIO, CONTRASENA, CORREO_ELECTRONICO, ROL)
        VALUES (@NOMBRE_USUARIO, @CONTRASENA, @CORREO_ELECTRONICO, @ROL);
        SELECT SCOPE_IDENTITY() AS ID_USUARIO;
    END
    ELSE
        SELECT -1 AS ID_USUARIO;
END;
GO

CREATE PROCEDURE SP_EXISTE_USUARIO
    @NOMBRE_USUARIO VARCHAR(50)
AS
BEGIN
    SELECT ID_USUARIO FROM USUARIO WHERE NOMBRE_USUARIO = @NOMBRE_USUARIO;
END;
GO

CREATE PROCEDURE SP_OBTENER_USUARIO
    @NOMBRE_USUARIO VARCHAR(50)
AS
BEGIN
    SELECT ID_USUARIO, NOMBRE_USUARIO, CORREO_ELECTRONICO, ROL, FECHA_CREACION, ESTADO
    FROM USUARIO
    WHERE NOMBRE_USUARIO = @NOMBRE_USUARIO;
END;
GO

CREATE PROCEDURE SP_OBTENER_CORREOS_USUARIOS
AS
BEGIN
    SELECT ID_USUARIO, CORREO_ELECTRONICO
    FROM USUARIO
    WHERE ESTADO = 1
    ORDER BY CORREO_ELECTRONICO;
END;
GO

