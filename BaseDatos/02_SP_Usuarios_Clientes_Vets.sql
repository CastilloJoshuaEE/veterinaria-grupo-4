CREATE PROCEDURE SP_INSERTAR_CLIENTE
    @CEDULA             VARCHAR(10),
    @NOMBRE             VARCHAR(50),
    @APELLIDO           VARCHAR(50),
    @TELEFONO           VARCHAR(15),
    @DIRECCION          VARCHAR(100) = NULL,
    @CORREO_ELECTRONICO VARCHAR(100) = NULL
AS
BEGIN
    IF NOT EXISTS (SELECT 1 FROM CLIENTE WHERE CEDULA = @CEDULA)
    BEGIN
        INSERT INTO CLIENTE (CEDULA, NOMBRE, APELLIDO, TELEFONO, DIRECCION, CORREO_ELECTRONICO)
        VALUES (@CEDULA, @NOMBRE, @APELLIDO, @TELEFONO, @DIRECCION, @CORREO_ELECTRONICO);
