CREATE PROCEDURE SP_OBTENER_CLIENTES_POR_MASCOTA
    @NOMBRE_MASCOTA VARCHAR(50),
    @NOMBRE_CLIENTE VARCHAR(50) = NULL
AS
BEGIN
    SELECT DISTINCT
        C.ID_CLIENTE, C.CEDULA, C.NOMBRE, C.APELLIDO, C.TELEFONO,
        C.DIRECCION, C.CORREO_ELECTRONICO, C.FECHA_REGISTRO
    FROM CLIENTE C
    INNER JOIN MASCOTA M ON C.ID_CLIENTE = M.ID_CLIENTE
    WHERE M.NOMBRE LIKE @NOMBRE_MASCOTA
      AND (@NOMBRE_CLIENTE IS NULL
           OR C.NOMBRE  LIKE @NOMBRE_CLIENTE
           OR C.APELLIDO LIKE @NOMBRE_CLIENTE)
    ORDER BY C.NOMBRE, C.APELLIDO;
END;
GO

