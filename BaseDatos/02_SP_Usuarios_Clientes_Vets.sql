CREATE OR ALTER PROCEDURE SP_BUSCAR_VETERINARIOS
    @TERMINO VARCHAR(100)
AS
BEGIN
    SET NOCOUNT ON;
    SELECT V.ID_VETERINARIO, 
           V.CEDULA, 
           V.NOMBRE, 
           V.APELLIDO, 
           V.TELEFONO,
           V.ID_ESPECIALIDAD,
           E.NOMBRE_ESPECIALIDAD AS NOMBRE_ESPECIALIDAD,
           V.CORREO_ELECTRONICO
    FROM VETERINARIO V
    LEFT JOIN ESPECIALIDAD_VETERINARIA E ON V.ID_ESPECIALIDAD = E.ID_ESPECIALIDAD
    WHERE V.NOMBRE   LIKE '%' + @TERMINO + '%' 
       OR V.APELLIDO LIKE '%' + @TERMINO + '%'
       OR V.CEDULA   LIKE '%' + @TERMINO + '%'
    ORDER BY V.NOMBRE ASC;
END;
GO

