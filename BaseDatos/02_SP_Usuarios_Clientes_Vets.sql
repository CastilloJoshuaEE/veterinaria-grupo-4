CREATE OR ALTER PROCEDURE SP_VALIDAR_USUARIO
    @EMAIL              VARCHAR(100),
    @CONTRASENA         VARCHAR(100)
AS
BEGIN
    SELECT 
        ID_USUARIO, 
        CORREO_ELECTRONICO AS EMAIL,
        ROL,
        ESTADO,
        -- Determinar si es veterinario o recepcionista
        CASE 
            WHEN EXISTS (SELECT 1 FROM VETERINARIO WHERE CORREO_ELECTRONICO = @EMAIL) 
                THEN 'VETERINARIO'
            WHEN EXISTS (SELECT 1 FROM RECEPCIONISTA WHERE CORREO_ELECTRONICO = @EMAIL) 
                THEN 'RECEPCIONISTA'
            ELSE ROL
        END AS ROL,
        -- Datos de la persona asociada
        CASE 
            WHEN EXISTS (SELECT 1 FROM VETERINARIO WHERE CORREO_ELECTRONICO = @EMAIL) 
                THEN (SELECT CONCAT(NOMBRE, ' ', APELLIDO) FROM VETERINARIO WHERE CORREO_ELECTRONICO = @EMAIL)
            WHEN EXISTS (SELECT 1 FROM RECEPCIONISTA WHERE CORREO_ELECTRONICO = @EMAIL) 
                THEN (SELECT CONCAT(NOMBRE, ' ', APELLIDO) FROM RECEPCIONISTA WHERE CORREO_ELECTRONICO = @EMAIL)
            ELSE 'Administrador'
        END AS NOMBRE_COMPLETO
    FROM USUARIO
    WHERE CORREO_ELECTRONICO = @EMAIL
      AND CONTRASENA = @CONTRASENA
      AND ESTADO = 1;
END;
GO

CREATE OR ALTER PROCEDURE SP_INSERTAR_USUARIO
    @EMAIL              VARCHAR(100),
    @CONTRASENA         VARCHAR(100),
    @ROL                VARCHAR(20) = 'ADMINISTRADOR'
