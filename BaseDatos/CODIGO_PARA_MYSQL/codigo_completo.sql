-- ============================================================

DELIMITER //

CREATE OR REPLACE PROCEDURE SP_INSERTAR_VACUNA_PROXIMA()
BEGIN
    DECLARE v_ID_MASCOTA INT;
    
    SELECT ID_MASCOTA INTO v_ID_MASCOTA FROM MASCOTA WHERE NOMBRE = 'Max';
    
    IF v_ID_MASCOTA IS NULL THEN
        SELECT MIN(ID_MASCOTA) INTO v_ID_MASCOTA FROM MASCOTA;
    END IF;
    
    INSERT INTO VACUNA_APLICADA (ID_MASCOTA, NOMBRE, DESCRIPCION, PERIODO_MESES, FECHA_APLICACION, FECHA_PROXIMA)
    VALUES (
        v_ID_MASCOTA,
        'Vacuna Sextuple Canina',
        'Proteccion contra moquillo, hepatitis, leptospirosis, parvovirus, parainfluenza',
        12,
        DATE_SUB(NOW(), INTERVAL 350 DAY),
        DATE_ADD(NOW(), INTERVAL 15 DAY)
    );
    
    SELECT 'Vacuna insertada correctamente' AS MENSAJE;
END //

DELIMITER ;

CALL SP_INSERTAR_VACUNA_PROXIMA();

