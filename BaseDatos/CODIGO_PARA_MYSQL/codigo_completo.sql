CREATE OR REPLACE PROCEDURE SP_OBTENER_HISTORIAL_MEDICO_POR_MASCOTA(
    IN p_ID_MASCOTA INT
)
BEGIN
    SELECT
        HM.ID_HISTORIAL,  
        AM.ID_ATENCION_MEDICA, 
        AM.FECHA, 
        C.ID_CITA, 
        S.NOMBRE_SERVICIO,
        CONCAT(V.NOMBRE, ' ', V.APELLIDO) AS VETERINARIO,
        AM.DIAGNOSTICO, 
        AM.TRATAMIENTO,
        AM.OBSERVACIONES AS OBSERVACIONES_ATENCION,
        (
            SELECT GROUP_CONCAT(IM.NOMBRE SEPARATOR ', ')
            FROM INSTRUMENTO_USADO IU
            JOIN INSTRUMENTO_MEDICO IM ON IU.ID_INSTRUMENTO = IM.ID_INSTRUMENTO
            WHERE IU.ID_ATENCION_MEDICA = AM.ID_ATENCION_MEDICA
        ) AS INSTRUMENTOS_USADOS,
        (
            SELECT GROUP_CONCAT(
                CONCAT(MED.NOMBRE, ' (', MR.DOSIS, ', ', MR.FRECUENCIA, ', ', MR.DURACION, ')')
                SEPARATOR ' | '
            )
            FROM MEDICAMENTO_RECETADO MR
            JOIN MEDICAMENTO MED ON MR.ID_MEDICAMENTO = MED.ID_MEDICAMENTO
            WHERE MR.ID_ATENCION_MEDICA = AM.ID_ATENCION_MEDICA
        ) AS MEDICAMENTOS_RECETADOS
    FROM HISTORIAL_MEDICO HM
    INNER JOIN ATENCION_MEDICA AM ON HM.ID_ATENCION_MEDICA = AM.ID_ATENCION_MEDICA
    INNER JOIN CITA C ON AM.ID_CITA = C.ID_CITA
    LEFT JOIN SERVICIO S ON C.ID_SERVICIO = S.ID_SERVICIO
    LEFT JOIN VETERINARIO V ON C.ID_VETERINARIO = V.ID_VETERINARIO
    WHERE HM.ID_MASCOTA = p_ID_MASCOTA
    ORDER BY AM.FECHA DESC;
END //

CREATE OR REPLACE PROCEDURE SP_ACTUALIZAR_FICHA_MEDICA(
    IN p_ID_MASCOTA INT,
    IN p_ALERGIAS VARCHAR(255),
    IN p_ENFERMEDADES_CRONICAS VARCHAR(255),
    IN p_OBSERVACIONES TEXT
)
BEGIN
    DECLARE EXIT HANDLER FOR SQLEXCEPTION
    BEGIN
        IF NOT @@autocommit THEN
            ROLLBACK;
        END IF;
        RESIGNAL;
    END;
    
    START TRANSACTION;
    
    IF NOT EXISTS (SELECT 1 FROM MASCOTA WHERE ID_MASCOTA = p_ID_MASCOTA) THEN
        SIGNAL SQLSTATE '45000' SET MESSAGE_TEXT = 'La mascota especificada no existe';
        ROLLBACK;
    ELSE
        IF EXISTS (SELECT 1 FROM FICHA_MEDICA WHERE ID_MASCOTA = p_ID_MASCOTA) THEN
            UPDATE FICHA_MEDICA
            SET ALERGIAS = IFNULL(p_ALERGIAS, ALERGIAS),
                ENFERMEDADES_CRONICAS = IFNULL(p_ENFERMEDADES_CRONICAS, ENFERMEDADES_CRONICAS),
                OBSERVACIONES = IFNULL(p_OBSERVACIONES, OBSERVACIONES),
                ULTIMA_ACTUALIZACION = NOW()
            WHERE ID_MASCOTA = p_ID_MASCOTA;
        ELSE
            INSERT INTO FICHA_MEDICA (ID_MASCOTA, ALERGIAS, ENFERMEDADES_CRONICAS, OBSERVACIONES)
            VALUES (p_ID_MASCOTA, p_ALERGIAS, p_ENFERMEDADES_CRONICAS, p_OBSERVACIONES);
        END IF;
    END IF;
    
    COMMIT;
END //

