DROP DATABASE IF EXISTS db_veterinaria;
CREATE DATABASE db_veterinaria;
USE db_veterinaria;
-- ─── Usuarios del sistema ────────────────────────────────────
CREATE TABLE USUARIO (
    ID_USUARIO          INT AUTO_INCREMENT PRIMARY KEY,
    CONTRASENA          VARCHAR(100) NOT NULL,
    CORREO_ELECTRONICO  VARCHAR(100) NOT NULL UNIQUE,
    ROL                 VARCHAR(20)  NOT NULL DEFAULT 'ADMINISTRADOR',
    FECHA_CREACION      DATETIME     DEFAULT NOW(),
    ESTADO              BOOLEAN      DEFAULT TRUE
);

-- ─── Recepcionistas ──────────────────────────────────────────
CREATE TABLE RECEPCIONISTA (
    ID_RECEPCIONISTA    INT AUTO_INCREMENT PRIMARY KEY,
    CEDULA              VARCHAR(10)  UNIQUE NOT NULL,
    NOMBRE              VARCHAR(50)  NOT NULL,
    APELLIDO            VARCHAR(50)  NOT NULL,
    TELEFONO            VARCHAR(15)  NOT NULL,
    DIRECCION           VARCHAR(100),
    CORREO_ELECTRONICO  VARCHAR(100) UNIQUE NOT NULL,
    FECHA_REGISTRO      DATETIME     DEFAULT NOW(),
    ESTADO              BOOLEAN      DEFAULT TRUE,
    CONSTRAINT FK_RECEPCIONISTA_USUARIO
        FOREIGN KEY (CORREO_ELECTRONICO)
        REFERENCES USUARIO(CORREO_ELECTRONICO)
        ON UPDATE CASCADE
);

-- ─── Clientes (dueños de mascotas) ───────────────────────────
CREATE TABLE CLIENTE (
    ID_CLIENTE          INT AUTO_INCREMENT PRIMARY KEY,
    CEDULA              VARCHAR(10)  UNIQUE NOT NULL,
    NOMBRE              VARCHAR(50)  NOT NULL,
    APELLIDO            VARCHAR(50)  NOT NULL,
    TELEFONO            VARCHAR(15)  NOT NULL,
    DIRECCION           VARCHAR(100),
    CORREO_ELECTRONICO  VARCHAR(100) UNIQUE,
    FECHA_REGISTRO      DATETIME     DEFAULT NOW()
);

-- ─── Especialidades veterinarias ─────────────────────────────
CREATE TABLE ESPECIALIDAD_VETERINARIA (
    ID_ESPECIALIDAD     INT AUTO_INCREMENT PRIMARY KEY,
    NOMBRE_ESPECIALIDAD VARCHAR(50) UNIQUE NOT NULL
);

-- ─── Veterinarios ────────────────────────────────────────────
CREATE TABLE VETERINARIO (
    ID_VETERINARIO      INT AUTO_INCREMENT PRIMARY KEY,
    CEDULA              VARCHAR(10) UNIQUE NOT NULL,
    NOMBRE              VARCHAR(50) NOT NULL,
    APELLIDO            VARCHAR(50) NOT NULL,
    TELEFONO            VARCHAR(15) NOT NULL,
    PAGO_MENSUAL        DECIMAL(10,2) DEFAULT 460.00,
    DIRECCION           VARCHAR(100),
    CORREO_ELECTRONICO  VARCHAR(100) NOT NULL UNIQUE,
    ID_ESPECIALIDAD     INT,
    FECHA_REGISTRO      DATETIME DEFAULT NOW(),
    CONSTRAINT FK_VETERINARIO_ESPECIALIDAD
        FOREIGN KEY (ID_ESPECIALIDAD)
        REFERENCES ESPECIALIDAD_VETERINARIA(ID_ESPECIALIDAD),
    CONSTRAINT FK_VETERINARIO_USUARIO
        FOREIGN KEY (CORREO_ELECTRONICO)
        REFERENCES USUARIO(CORREO_ELECTRONICO)
        ON UPDATE CASCADE
);
-- ─── Mascotas ────────────────────────────────────────────────
CREATE TABLE MASCOTA (
    ID_MASCOTA      INT AUTO_INCREMENT PRIMARY KEY,
    ID_CLIENTE      INT           NOT NULL,
    NOMBRE          VARCHAR(50)   NOT NULL,
    ESPECIE         VARCHAR(30)   NOT NULL,
    RAZA            VARCHAR(50),
    SEXO            CHAR(1)       CHECK (SEXO IN ('M', 'H')),
    FECHA_NACIMIENTO DATE,
    PESO            DECIMAL(5,2),
    COLOR           VARCHAR(30),
    FOTO            LONGBLOB      NULL,
    FECHA_REGISTRO  DATETIME      DEFAULT NOW(),
    FOREIGN KEY (ID_CLIENTE) REFERENCES CLIENTE(ID_CLIENTE)
);

-- ─── Fichas médicas ──────────────────────────────────────────
CREATE TABLE FICHA_MEDICA (
    ID_FICHA              INT AUTO_INCREMENT PRIMARY KEY,
    ID_MASCOTA            INT NOT NULL,
    ALERGIAS              VARCHAR(255),
    ENFERMEDADES_CRONICAS VARCHAR(255),
    OBSERVACIONES         TEXT,
    ULTIMA_ACTUALIZACION  DATETIME DEFAULT NOW(),
    FOREIGN KEY (ID_MASCOTA) REFERENCES MASCOTA(ID_MASCOTA)
);

-- ─── Servicios ───────────────────────────────────────────────
CREATE TABLE SERVICIO (
    ID_SERVICIO       INT AUTO_INCREMENT PRIMARY KEY,
    NOMBRE_SERVICIO   VARCHAR(50)   NOT NULL,
    DESCRIPCION       VARCHAR(255),
    PRECIO            DECIMAL(10,2) NOT NULL,
    DURACION_ESTIMADA INT,                          -- minutos
    ESTADO            BOOLEAN       DEFAULT TRUE
);

-- ─── Relación Servicio ↔ Veterinario (N:M) ───────────────────
CREATE TABLE SERVICIO_VETERINARIO (
    ID_SERVICIO_VETERINARIO INT AUTO_INCREMENT PRIMARY KEY,
    ID_SERVICIO             INT NOT NULL,
    ID_VETERINARIO          INT NOT NULL,
    FECHA_ASIGNACION        DATETIME DEFAULT NOW(),
    FOREIGN KEY (ID_SERVICIO)    REFERENCES SERVICIO(ID_SERVICIO),
    FOREIGN KEY (ID_VETERINARIO) REFERENCES VETERINARIO(ID_VETERINARIO),
    CONSTRAINT UQ_SERVICIO_VETERINARIO UNIQUE (ID_SERVICIO, ID_VETERINARIO)
