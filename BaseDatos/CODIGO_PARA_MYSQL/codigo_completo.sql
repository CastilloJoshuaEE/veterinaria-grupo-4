CREATE OR REPLACE PROCEDURE SP_OBTENER_HISTORIAL_VACUNACION(
    IN p_ID_MASCOTA INT
)
BEGIN
    SELECT VA.ID_VACUNA, VA.NOMBRE, VA.DESCRIPCION, VA.FECHA_APLICACION, VA.FECHA_PROXIMA,
           CASE
               WHEN VA.FECHA_PROXIMA < NOW() THEN 'VENCIDA'
               WHEN DATEDIFF(VA.FECHA_PROXIMA, NOW()) <= 30 THEN 'POR VENCER'
               ELSE 'VIGENTE'
           END AS ESTADO,
           DATEDIFF(VA.FECHA_PROXIMA, NOW()) AS DIAS_RESTANTES
    FROM VACUNA_APLICADA VA
    WHERE VA.ID_MASCOTA = p_ID_MASCOTA
    ORDER BY VA.FECHA_APLICACION DESC;
