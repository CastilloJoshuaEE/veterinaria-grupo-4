    IN p_ID_VETERINARIO INT,
    IN p_FECHA_HORA DATETIME,
    IN p_OBSERVACIONES VARCHAR(255)
)
BEGIN
    DECLARE v_ID_CITA INT;
    DECLARE EXIT HANDLER FOR SQLEXCEPTION
    BEGIN
        ROLLBACK;
        SELECT -2 AS ID_CITA;
    END;
    
