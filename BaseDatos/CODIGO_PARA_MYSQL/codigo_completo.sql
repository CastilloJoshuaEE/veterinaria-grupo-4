    WHERE F.ID_FACTURA = p_ID_FACTURA
    LIMIT 1;
    
    SELECT 
        S.NOMBRE_SERVICIO,
        IFNULL(S.DESCRIPCION, '') AS DESCRIPCION_SERVICIO,
        DF.PRECIO_UNITARIO,
        DF.TOTAL AS TOTAL_SERVICIO,
        IFNULL(CONCAT(V.NOMBRE, ' ', V.APELLIDO), 'No asignado') AS VETERINARIO
    FROM DETALLE_FACTURA DF
    INNER JOIN SERVICIO S ON DF.ID_SERVICIO = S.ID_SERVICIO
    LEFT JOIN ATENCION_MEDICA AM ON DF.ID_ATENCION_MEDICA = AM.ID_ATENCION_MEDICA
    LEFT JOIN CITA C ON AM.ID_CITA = C.ID_CITA
    LEFT JOIN VETERINARIO V ON C.ID_VETERINARIO = V.ID_VETERINARIO
    WHERE DF.ID_FACTURA = p_ID_FACTURA
      AND S.NOMBRE_SERVICIO NOT IN ('MEDICAMENTO', 'INSTRUMENTO')
    LIMIT 1;
    
    SELECT DISTINCT
        M.NOMBRE AS NOMBRE_MASCOTA,
        IFNULL(M.ESPECIE, '') AS ESPECIE,
        IFNULL(M.RAZA, '') AS RAZA,
        IFNULL(CAST(AM.DIAGNOSTICO AS CHAR), '') AS DIAGNOSTICO,
        IFNULL(CAST(AM.TRATAMIENTO AS CHAR), '') AS TRATAMIENTO
    FROM DETALLE_FACTURA DF
    INNER JOIN ATENCION_MEDICA AM ON DF.ID_ATENCION_MEDICA = AM.ID_ATENCION_MEDICA
    INNER JOIN CITA C ON AM.ID_CITA = C.ID_CITA
    INNER JOIN MASCOTA M ON C.ID_MASCOTA = M.ID_MASCOTA
    WHERE DF.ID_FACTURA = p_ID_FACTURA;
    
    SELECT DISTINCT
        MED.NOMBRE AS MEDICAMENTO,
        MED.PRECIO,
        IFNULL(MR.DOSIS, '') AS DOSIS,
        IFNULL(MR.FRECUENCIA, '') AS FRECUENCIA,
        IFNULL(MR.DURACION, '') AS DURACION
    FROM DETALLE_FACTURA DF
    INNER JOIN ATENCION_MEDICA AM ON DF.ID_ATENCION_MEDICA = AM.ID_ATENCION_MEDICA
    INNER JOIN MEDICAMENTO_RECETADO MR ON AM.ID_ATENCION_MEDICA = MR.ID_ATENCION_MEDICA
    INNER JOIN MEDICAMENTO MED ON MR.ID_MEDICAMENTO = MED.ID_MEDICAMENTO
    WHERE DF.ID_FACTURA = p_ID_FACTURA;
    
    SELECT DISTINCT
        IM.NOMBRE AS INSTRUMENTO,
        IM.COSTO_USO
    FROM DETALLE_FACTURA DF
    INNER JOIN ATENCION_MEDICA AM ON DF.ID_ATENCION_MEDICA = AM.ID_ATENCION_MEDICA
    INNER JOIN INSTRUMENTO_USADO IU ON AM.ID_ATENCION_MEDICA = IU.ID_ATENCION_MEDICA
    INNER JOIN INSTRUMENTO_MEDICO IM ON IU.ID_INSTRUMENTO = IM.ID_INSTRUMENTO
    WHERE DF.ID_FACTURA = p_ID_FACTURA;
    
    SELECT DISTINCT
        VA.NOMBRE AS NOMBRE_VACUNA,
        IFNULL(VA.DESCRIPCION, '') AS DESCRIPCION,
        VA.FECHA_APLICACION,
        VA.FECHA_PROXIMA
    FROM DETALLE_FACTURA DF
    INNER JOIN ATENCION_MEDICA AM ON DF.ID_ATENCION_MEDICA = AM.ID_ATENCION_MEDICA
    INNER JOIN CITA C ON AM.ID_CITA = C.ID_CITA
    INNER JOIN MASCOTA M ON C.ID_MASCOTA = M.ID_MASCOTA
    INNER JOIN VACUNA_APLICADA VA ON M.ID_MASCOTA = VA.ID_MASCOTA
    WHERE DF.ID_FACTURA = p_ID_FACTURA;
END //


CREATE PROCEDURE SP_GENERAR_FACTURA_ATENCION(
    IN p_ID_ATENCION_MEDICA INT,
    IN p_METODO VARCHAR(255),
    IN p_CUENTA_ORIGEN VARCHAR(20),
    IN p_CUENTA_DESTINO VARCHAR(20)
)
BEGIN
    DECLARE v_ID_CLIENTE INT;
    DECLARE v_ID_SERVICIO INT;
    DECLARE v_ID_MASCOTA INT;
    DECLARE v_PRECIO_SERVICIO DECIMAL(10,2);
    DECLARE v_SUBTOTAL DECIMAL(10,2);
    DECLARE v_IVA DECIMAL(10,2);
    DECLARE v_TOTAL DECIMAL(10,2);
    DECLARE v_ID_FACTURA INT;
    DECLARE v_ID_METODO_PAGO INT;
    DECLARE v_ID_SERVICIO_MED INT;
    DECLARE v_ID_SERVICIO_INSTR INT;
    DECLARE v_ID_CITA INT;
    DECLARE v_ERROR_MENSAJE TEXT;
    DECLARE v_ERROR_NUMERO INT;
    DECLARE v_ERROR_LINEA INT;
    
    DECLARE EXIT HANDLER FOR SQLEXCEPTION
    BEGIN
        GET DIAGNOSTICS CONDITION 1
            v_ERROR_MENSAJE = MESSAGE_TEXT,
            v_ERROR_NUMERO = MYSQL_ERRNO;
        
        IF @@autocommit = 0 THEN
            ROLLBACK;
        END IF;
        
        SELECT -1 AS ID_FACTURA, v_ERROR_MENSAJE AS ERROR_MENSAJE, v_ERROR_NUMERO AS ERROR_NUMERO, v_ERROR_LINEA AS ERROR_LINEA;
    END;
    
    START TRANSACTION;
    
    SELECT ID_CITA INTO v_ID_CITA 
    FROM ATENCION_MEDICA 
    WHERE ID_ATENCION_MEDICA = p_ID_ATENCION_MEDICA;
    
    IF v_ID_CITA IS NULL THEN
        SET v_ERROR_MENSAJE = CONCAT('No existe la atencion medica con ID: ', p_ID_ATENCION_MEDICA);
        SIGNAL SQLSTATE '45000' SET MESSAGE_TEXT = v_ERROR_MENSAJE;
    END IF;
    
    SELECT ID_CLIENTE, ID_SERVICIO, ID_MASCOTA INTO v_ID_CLIENTE, v_ID_SERVICIO, v_ID_MASCOTA
    FROM CITA 
    WHERE ID_CITA = v_ID_CITA;
    
    SELECT IFNULL(PRECIO, 0) INTO v_PRECIO_SERVICIO
    FROM SERVICIO 
    WHERE ID_SERVICIO = v_ID_SERVICIO;
    
    IF v_ID_CLIENTE IS NULL THEN
        SET v_ERROR_MENSAJE = CONCAT('No se encontro el cliente para la cita ID: ', v_ID_CITA);
        SIGNAL SQLSTATE '45000' SET MESSAGE_TEXT = v_ERROR_MENSAJE;
    END IF;
    
    IF v_ID_SERVICIO IS NULL THEN
        SET v_ERROR_MENSAJE = CONCAT('No se encontro el servicio para la cita ID: ', v_ID_CITA);
        SIGNAL SQLSTATE '45000' SET MESSAGE_TEXT = v_ERROR_MENSAJE;
    END IF;
    
    SELECT ID_SERVICIO INTO v_ID_SERVICIO_MED FROM SERVICIO WHERE NOMBRE_SERVICIO = 'MEDICAMENTO';
    SELECT ID_SERVICIO INTO v_ID_SERVICIO_INSTR FROM SERVICIO WHERE NOMBRE_SERVICIO = 'INSTRUMENTO';
    
    IF v_ID_SERVICIO_MED IS NULL THEN
        INSERT INTO SERVICIO (NOMBRE_SERVICIO, DESCRIPCION, PRECIO, DURACION_ESTIMADA, ESTADO)
        VALUES ('MEDICAMENTO', 'Medicamentos recetados', 0, 0, 1);
        SET v_ID_SERVICIO_MED = LAST_INSERT_ID();
    END IF;
    
    IF v_ID_SERVICIO_INSTR IS NULL THEN
        INSERT INTO SERVICIO (NOMBRE_SERVICIO, DESCRIPCION, PRECIO, DURACION_ESTIMADA, ESTADO)
        VALUES ('INSTRUMENTO', 'Instrumentos utilizados', 0, 0, 1);
        SET v_ID_SERVICIO_INSTR = LAST_INSERT_ID();
    END IF;
    
    SET v_SUBTOTAL = IFNULL(v_PRECIO_SERVICIO, 0);
    
    SELECT v_SUBTOTAL + IFNULL(SUM(M.PRECIO), 0) INTO v_SUBTOTAL
    FROM MEDICAMENTO_RECETADO MR
    INNER JOIN MEDICAMENTO M ON MR.ID_MEDICAMENTO = M.ID_MEDICAMENTO
    WHERE MR.ID_ATENCION_MEDICA = p_ID_ATENCION_MEDICA;
    
    SELECT v_SUBTOTAL + IFNULL(SUM(IM.COSTO_USO), 0) INTO v_SUBTOTAL
    FROM INSTRUMENTO_USADO IU
    INNER JOIN INSTRUMENTO_MEDICO IM ON IU.ID_INSTRUMENTO = IM.ID_INSTRUMENTO
    WHERE IU.ID_ATENCION_MEDICA = p_ID_ATENCION_MEDICA;
    
    SET v_IVA = v_SUBTOTAL * 0.12;
    SET v_TOTAL = v_SUBTOTAL + v_IVA;
    
    INSERT INTO METODO_PAGO (METODO, VALOR_TOTAL, CUENTA_ORIGEN, CUENTA_DESTINO)
    VALUES (p_METODO, v_TOTAL, p_CUENTA_ORIGEN, p_CUENTA_DESTINO);
    SET v_ID_METODO_PAGO = LAST_INSERT_ID();
    
    INSERT INTO FACTURA (ID_CLIENTE, SUBTOTAL, IVA, TOTAL, ESTADO, ID_METODO_PAGO)
    VALUES (v_ID_CLIENTE, v_SUBTOTAL, v_IVA, v_TOTAL, 'PAGADA', v_ID_METODO_PAGO);
    SET v_ID_FACTURA = LAST_INSERT_ID();
    
    INSERT INTO DETALLE_FACTURA (ID_FACTURA, ID_SERVICIO, CANTIDAD, PRECIO_UNITARIO, TOTAL, ID_ATENCION_MEDICA)
    VALUES (v_ID_FACTURA, v_ID_SERVICIO, 1, v_PRECIO_SERVICIO, v_PRECIO_SERVICIO, p_ID_ATENCION_MEDICA);
    
    INSERT INTO DETALLE_FACTURA (ID_FACTURA, ID_SERVICIO, CANTIDAD, PRECIO_UNITARIO, TOTAL, ID_ATENCION_MEDICA)
    SELECT v_ID_FACTURA, v_ID_SERVICIO_MED, 1, M.PRECIO, M.PRECIO, p_ID_ATENCION_MEDICA
    FROM MEDICAMENTO_RECETADO MR
    INNER JOIN MEDICAMENTO M ON MR.ID_MEDICAMENTO = M.ID_MEDICAMENTO
    WHERE MR.ID_ATENCION_MEDICA = p_ID_ATENCION_MEDICA;
    
    INSERT INTO DETALLE_FACTURA (ID_FACTURA, ID_SERVICIO, CANTIDAD, PRECIO_UNITARIO, TOTAL, ID_ATENCION_MEDICA)
    SELECT v_ID_FACTURA, v_ID_SERVICIO_INSTR, 1, IM.COSTO_USO, IM.COSTO_USO, p_ID_ATENCION_MEDICA
    FROM INSTRUMENTO_USADO IU
    INNER JOIN INSTRUMENTO_MEDICO IM ON IU.ID_INSTRUMENTO = IM.ID_INSTRUMENTO
    WHERE IU.ID_ATENCION_MEDICA = p_ID_ATENCION_MEDICA;
    
    COMMIT;
    SELECT v_ID_FACTURA AS ID_FACTURA;
END //


