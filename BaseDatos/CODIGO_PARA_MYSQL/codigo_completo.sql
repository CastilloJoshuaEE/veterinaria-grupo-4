CREATE OR REPLACE PROCEDURE SP_OBTENER_VACUNAS_PROXIMAS_A_VENCER(
    IN p_DIAS_ANTICIPACION INT
)
BEGIN
    IF p_DIAS_ANTICIPACION IS NULL THEN
        SET p_DIAS_ANTICIPACION = 30;
    END IF;
    
    SELECT 
        VA.ID_VACUNA, 
        VA.NOMBRE,
        IFNULL(VA.DESCRIPCION, '') AS DESCRIPCION,
        IFNULL(VA.PERIODO_MESES, 0) AS PERIODO_MESES,
        VA.FECHA_APLICACION, 
        VA.FECHA_PROXIMA,
        VA.ID_MASCOTA,
        IFNULL(M.NOMBRE, 'Mascota no disponible') AS NOMBRE_MASCOTA
    FROM VACUNA_APLICADA VA
    INNER JOIN MASCOTA M ON VA.ID_MASCOTA = M.ID_MASCOTA
    WHERE VA.FECHA_PROXIMA BETWEEN NOW() AND DATE_ADD(NOW(), INTERVAL p_DIAS_ANTICIPACION DAY)
    ORDER BY VA.FECHA_PROXIMA ASC;
END //

