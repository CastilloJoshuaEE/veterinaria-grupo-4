CREATE OR REPLACE PROCEDURE SP_VALIDAR_USUARIO(
    IN p_EMAIL VARCHAR(100),
    IN p_CONTRASENA VARCHAR(100)
)
BEGIN
    SELECT 
        U.ID_USUARIO, 
        U.CORREO_ELECTRONICO AS EMAIL,
        U.ROL,
        U.ESTADO,
        CASE 
            WHEN EXISTS (SELECT 1 FROM VETERINARIO WHERE CORREO_ELECTRONICO = p_EMAIL) 
                THEN 'VETERINARIO'
            WHEN EXISTS (SELECT 1 FROM RECEPCIONISTA WHERE CORREO_ELECTRONICO = p_EMAIL) 
                THEN 'RECEPCIONISTA'
            ELSE U.ROL
        END AS ROL_EFECTIVO,
        CASE 
            WHEN EXISTS (SELECT 1 FROM VETERINARIO WHERE CORREO_ELECTRONICO = p_EMAIL) 
                THEN (SELECT CONCAT(NOMBRE, ' ', APELLIDO) FROM VETERINARIO WHERE CORREO_ELECTRONICO = p_EMAIL)
            WHEN EXISTS (SELECT 1 FROM RECEPCIONISTA WHERE CORREO_ELECTRONICO = p_EMAIL) 
                THEN (SELECT CONCAT(NOMBRE, ' ', APELLIDO) FROM RECEPCIONISTA WHERE CORREO_ELECTRONICO = p_EMAIL)
            ELSE 'Administrador'
        END AS NOMBRE_COMPLETO
    FROM USUARIO U
    WHERE U.CORREO_ELECTRONICO = p_EMAIL
      AND U.CONTRASENA = p_CONTRASENA
      AND U.ESTADO = TRUE;
END //

CREATE OR REPLACE PROCEDURE SP_INSERTAR_USUARIO(
    IN p_EMAIL VARCHAR(100),
    IN p_CONTRASENA VARCHAR(100),
    IN p_ROL VARCHAR(20)
)
BEGIN
    DECLARE v_ID_USUARIO INT;
    
    IF NOT EXISTS (SELECT 1 FROM USUARIO WHERE CORREO_ELECTRONICO = p_EMAIL) THEN
        INSERT INTO USUARIO (CORREO_ELECTRONICO, CONTRASENA, ROL)
        VALUES (p_EMAIL, p_CONTRASENA, IFNULL(p_ROL, 'ADMINISTRADOR'));
        SELECT LAST_INSERT_ID() AS ID_USUARIO;
    ELSE
        SELECT -1 AS ID_USUARIO;
    END IF;
END //

CREATE OR REPLACE PROCEDURE SP_EXISTE_USUARIO(
    IN p_EMAIL VARCHAR(100)
)
BEGIN
    SELECT ID_USUARIO FROM USUARIO WHERE CORREO_ELECTRONICO = p_EMAIL;
END //

CREATE OR REPLACE PROCEDURE SP_OBTENER_USUARIO(
    IN p_EMAIL VARCHAR(100)
)
BEGIN
    SELECT 
        U.ID_USUARIO,
        U.CORREO_ELECTRONICO,
        U.ROL,
        U.FECHA_CREACION,
        U.ESTADO,
        CASE 
            WHEN EXISTS (SELECT 1 FROM VETERINARIO WHERE CORREO_ELECTRONICO = U.CORREO_ELECTRONICO) 
                THEN 'VETERINARIO'
            WHEN EXISTS (SELECT 1 FROM RECEPCIONISTA WHERE CORREO_ELECTRONICO = U.CORREO_ELECTRONICO) 
                THEN 'RECEPCIONISTA'
            ELSE U.ROL
        END AS ROL_EFECTIVO,
        CASE 
            WHEN EXISTS (SELECT 1 FROM VETERINARIO V WHERE V.CORREO_ELECTRONICO = U.CORREO_ELECTRONICO) 
                THEN (SELECT CONCAT(V.NOMBRE, ' ', V.APELLIDO) FROM VETERINARIO V WHERE V.CORREO_ELECTRONICO = U.CORREO_ELECTRONICO)
            WHEN EXISTS (SELECT 1 FROM RECEPCIONISTA R WHERE R.CORREO_ELECTRONICO = U.CORREO_ELECTRONICO) 
                THEN (SELECT CONCAT(R.NOMBRE, ' ', R.APELLIDO) FROM RECEPCIONISTA R WHERE R.CORREO_ELECTRONICO = U.CORREO_ELECTRONICO)
            ELSE 'Administrador'
        END AS NOMBRE_COMPLETO
    FROM USUARIO U
    WHERE U.CORREO_ELECTRONICO = p_EMAIL;
END //

CREATE OR REPLACE PROCEDURE SP_OBTENER_CORREOS_USUARIOS()
BEGIN
    SELECT ID_USUARIO, CORREO_ELECTRONICO
    FROM USUARIO
    WHERE ESTADO = TRUE
    ORDER BY CORREO_ELECTRONICO;
END //

