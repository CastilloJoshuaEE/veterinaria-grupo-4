CREATE OR REPLACE PROCEDURE SP_ASIGNAR_MULTIPLES_VETERINARIOS(
    IN p_ID_SERVICIO INT,
    IN p_IDS_VETERINARIOS TEXT
)
BEGIN
    DECLARE v_CONTADOR INT DEFAULT 0;
    DECLARE v_ID_VETERINARIO INT;
    DECLARE v_done INT DEFAULT FALSE;
    DECLARE v_cursor CURSOR FOR SELECT DISTINCT CAST(value AS UNSIGNED) FROM (SELECT SUBSTRING_INDEX(SUBSTRING_INDEX(p_IDS_VETERINARIOS, ',', n), ',', -1) AS value
        FROM (SELECT 1 n UNION SELECT 2 UNION SELECT 3 UNION SELECT 4 UNION SELECT 5 UNION SELECT 6 UNION SELECT 7 UNION SELECT 8 UNION SELECT 9 UNION SELECT 10) AS numbers
        WHERE CHAR_LENGTH(p_IDS_VETERINARIOS) - CHAR_LENGTH(REPLACE(p_IDS_VETERINARIOS, ',', '')) >= n-1) AS ids;
    DECLARE CONTINUE HANDLER FOR NOT FOUND SET v_done = TRUE;
    
    OPEN v_cursor;
    
    read_loop: LOOP
        FETCH v_cursor INTO v_ID_VETERINARIO;
        IF v_done THEN
            LEAVE read_loop;
        END IF;
        
        IF NOT EXISTS (SELECT 1 FROM SERVICIO_VETERINARIO WHERE ID_SERVICIO = p_ID_SERVICIO AND ID_VETERINARIO = v_ID_VETERINARIO) THEN
            INSERT INTO SERVICIO_VETERINARIO (ID_SERVICIO, ID_VETERINARIO)
            VALUES (p_ID_SERVICIO, v_ID_VETERINARIO);
            SET v_CONTADOR = v_CONTADOR + 1;
        END IF;
    END LOOP;
    
    CLOSE v_cursor;
    SELECT v_CONTADOR AS ASIGNACIONES_REALIZADAS;
END //

