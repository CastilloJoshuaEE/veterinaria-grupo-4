CREATE OR REPLACE PROCEDURE SP_REGISTRAR_RECORDATORIO(
    IN p_CORREO_USUARIO VARCHAR(100),
    IN p_ID_CITA INT,
    IN p_ID_VACUNA INT,
    IN p_TIPO VARCHAR(20),
    IN p_MENSAJE VARCHAR(500),
    IN p_ANTICIPACION VARCHAR(20)
)
BEGIN
    DECLARE v_ID_USUARIO INT DEFAULT NULL;
    DECLARE v_FECHA_EVENTO DATETIME;
    
    IF p_CORREO_USUARIO IS NOT NULL THEN
        SELECT ID_USUARIO INTO v_ID_USUARIO FROM USUARIO WHERE CORREO_ELECTRONICO = p_CORREO_USUARIO;
    END IF;
    
    IF p_TIPO = 'CITA' THEN
        SELECT FECHA_HORA INTO v_FECHA_EVENTO FROM CITA WHERE ID_CITA = p_ID_CITA;
    ELSE
        SELECT FECHA_PROXIMA INTO v_FECHA_EVENTO FROM VACUNA_APLICADA WHERE ID_VACUNA = p_ID_VACUNA;
    END IF;
    
    INSERT INTO RECORDATORIO_ENVIADO (ID_USUARIO, ID_CITA, ID_VACUNA, TIPO, MENSAJE, FECHA_ENVIO)
    VALUES (
        v_ID_USUARIO, p_ID_CITA, p_ID_VACUNA, p_TIPO, p_MENSAJE,
        CASE
            WHEN p_ANTICIPACION = '15_DIAS' THEN DATE_SUB(v_FECHA_EVENTO, INTERVAL 15 DAY)
            WHEN p_ANTICIPACION = '7_DIAS' THEN DATE_SUB(v_FECHA_EVENTO, INTERVAL 7 DAY)
            WHEN p_ANTICIPACION = '1_MES' THEN DATE_SUB(v_FECHA_EVENTO, INTERVAL 1 MONTH)
            ELSE NOW()
        END
    );
    SELECT LAST_INSERT_ID() AS ID_RECORDATORIO;
END //

