    ELSE
        INSERT INTO CITA (ID_CLIENTE, ID_MASCOTA, ID_SERVICIO, ID_VETERINARIO, FECHA_HORA, OBSERVACIONES)
        VALUES (p_ID_CLIENTE, p_ID_MASCOTA, p_ID_SERVICIO, p_ID_VETERINARIO, p_FECHA_HORA, p_OBSERVACIONES);
        
        SET v_ID_CITA = LAST_INSERT_ID();
