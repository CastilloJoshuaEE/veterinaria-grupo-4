CREATE OR REPLACE PROCEDURE SP_ACTUALIZAR_SERVICIO(
    IN p_ID_SERVICIO INT,
    IN p_NOMBRE_SERVICIO VARCHAR(50),
    IN p_DESCRIPCION VARCHAR(255),
    IN p_PRECIO DECIMAL(10,2),
    IN p_DURACION_ESTIMADA INT,
    IN p_ESTADO BOOLEAN
)
BEGIN
    DECLARE EXIT HANDLER FOR SQLEXCEPTION
    BEGIN
        SELECT 0 AS FILAS_AFECTADAS;
    END;
    
    IF NOT EXISTS (SELECT 1 FROM SERVICIO WHERE ID_SERVICIO = p_ID_SERVICIO) THEN
        SELECT 0 AS FILAS_AFECTADAS;
    ELSEIF EXISTS (SELECT 1 FROM SERVICIO WHERE NOMBRE_SERVICIO = p_NOMBRE_SERVICIO AND ID_SERVICIO <> p_ID_SERVICIO) THEN
        SELECT -1 AS FILAS_AFECTADAS;
    ELSE
        UPDATE SERVICIO
        SET NOMBRE_SERVICIO = p_NOMBRE_SERVICIO, DESCRIPCION = p_DESCRIPCION,
            PRECIO = p_PRECIO, DURACION_ESTIMADA = p_DURACION_ESTIMADA, ESTADO = p_ESTADO
        WHERE ID_SERVICIO = p_ID_SERVICIO;
        SELECT ROW_COUNT() AS FILAS_AFECTADAS;
    END IF;
END //

