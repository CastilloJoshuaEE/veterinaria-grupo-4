CREATE OR REPLACE PROCEDURE SP_OBTENER_CLIENTES_POR_NOMBRE(
    IN p_NOMBRE VARCHAR(50)
)
BEGIN
    SELECT ID_CLIENTE, CEDULA, NOMBRE, APELLIDO, TELEFONO,
           DIRECCION, CORREO_ELECTRONICO, FECHA_REGISTRO
    FROM CLIENTE
    WHERE NOMBRE LIKE CONCAT('%', p_NOMBRE, '%') 
       OR APELLIDO LIKE CONCAT('%', p_NOMBRE, '%')
    ORDER BY NOMBRE, APELLIDO;
END //

CREATE OR REPLACE PROCEDURE SP_OBTENER_CLIENTES_POR_MASCOTA(
    IN p_NOMBRE_MASCOTA VARCHAR(50),
    IN p_NOMBRE_CLIENTE VARCHAR(50)
)
BEGIN
    SELECT DISTINCT
        C.ID_CLIENTE, C.CEDULA, C.NOMBRE, C.APELLIDO, C.TELEFONO,
        C.DIRECCION, C.CORREO_ELECTRONICO, C.FECHA_REGISTRO
    FROM CLIENTE C
    INNER JOIN MASCOTA M ON C.ID_CLIENTE = M.ID_CLIENTE
    WHERE M.NOMBRE LIKE CONCAT('%', p_NOMBRE_MASCOTA, '%')
      AND (p_NOMBRE_CLIENTE IS NULL
           OR C.NOMBRE LIKE CONCAT('%', p_NOMBRE_CLIENTE, '%')
           OR C.APELLIDO LIKE CONCAT('%', p_NOMBRE_CLIENTE, '%'))
    ORDER BY C.NOMBRE, C.APELLIDO;
END //

