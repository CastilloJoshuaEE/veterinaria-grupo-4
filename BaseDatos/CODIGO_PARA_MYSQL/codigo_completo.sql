CREATE OR REPLACE PROCEDURE SP_INSERTAR_CLIENTE(
    IN p_CEDULA VARCHAR(10),
    IN p_NOMBRE VARCHAR(50),
    IN p_APELLIDO VARCHAR(50),
    IN p_TELEFONO VARCHAR(15),
    IN p_DIRECCION VARCHAR(100),
    IN p_CORREO_ELECTRONICO VARCHAR(100)
)
BEGIN
    IF NOT EXISTS (SELECT 1 FROM CLIENTE WHERE CEDULA = p_CEDULA) THEN
        INSERT INTO CLIENTE (CEDULA, NOMBRE, APELLIDO, TELEFONO, DIRECCION, CORREO_ELECTRONICO)
        VALUES (p_CEDULA, p_NOMBRE, p_APELLIDO, p_TELEFONO, p_DIRECCION, p_CORREO_ELECTRONICO);
        SELECT LAST_INSERT_ID() AS ID_CLIENTE;
    ELSE
        SELECT -1 AS ID_CLIENTE;
    END IF;
END //

CREATE OR REPLACE PROCEDURE SP_ACTUALIZAR_CLIENTE(
    IN p_ID_CLIENTE INT,
    IN p_CEDULA VARCHAR(10),
    IN p_NOMBRE VARCHAR(50),
    IN p_APELLIDO VARCHAR(50),
    IN p_TELEFONO VARCHAR(15),
    IN p_DIRECCION VARCHAR(100),
    IN p_CORREO_ELECTRONICO VARCHAR(100)
)
BEGIN
    UPDATE CLIENTE
    SET CEDULA = p_CEDULA, NOMBRE = p_NOMBRE, APELLIDO = p_APELLIDO,
        TELEFONO = p_TELEFONO, DIRECCION = p_DIRECCION,
        CORREO_ELECTRONICO = p_CORREO_ELECTRONICO
    WHERE ID_CLIENTE = p_ID_CLIENTE;
    SELECT ROW_COUNT() AS FILAS_AFECTADAS;
END //

CREATE OR REPLACE PROCEDURE SP_ELIMINAR_CLIENTE(
    IN p_ID_CLIENTE INT
)
BEGIN
    DECLARE EXIT HANDLER FOR SQLEXCEPTION
    BEGIN
        ROLLBACK;
        SELECT 0 AS RESULTADO;
    END;
    
    START TRANSACTION;
    DELETE FROM CLIENTE WHERE ID_CLIENTE = p_ID_CLIENTE;
    COMMIT;
    SELECT 1 AS RESULTADO;
END //

