CREATE OR REPLACE PROCEDURE SP_OBTENER_CITAS_POR_RANGO_FECHAS(
    IN p_FECHA_INICIO DATE,
    IN p_FECHA_FIN DATE
)
BEGIN
    SELECT C.ID_CITA, C.ID_CLIENTE,
           CONCAT(CL.NOMBRE, ' ', CL.APELLIDO) AS NOMBRE_CLIENTE,
           C.ID_MASCOTA, M.NOMBRE AS NOMBRE_MASCOTA,
           C.ID_SERVICIO, S.NOMBRE_SERVICIO,
           C.FECHA_HORA, C.ESTADO, C.OBSERVACIONES,
           CASE C.ESTADO
               WHEN 'PENDIENTE' THEN 'Pendiente'
               WHEN 'REALIZADA' THEN 'Realizada'
               WHEN 'CANCELADA' THEN 'Cancelada'
               ELSE C.ESTADO
           END AS ESTADO_DESCRIPCION,
           CASE WHEN S.NOMBRE_SERVICIO LIKE '%Vacuna%' THEN 'Vacunacion' ELSE 'Consulta' END AS TIPO_CITA
    FROM CITA C
    INNER JOIN CLIENTE CL ON C.ID_CLIENTE = CL.ID_CLIENTE
    INNER JOIN MASCOTA M ON C.ID_MASCOTA = M.ID_MASCOTA
    INNER JOIN SERVICIO S ON C.ID_SERVICIO = S.ID_SERVICIO
    WHERE DATE(C.FECHA_HORA) BETWEEN p_FECHA_INICIO AND p_FECHA_FIN
    ORDER BY C.FECHA_HORA;
END //

CREATE OR REPLACE PROCEDURE SP_VERIFICAR_RECORDATORIOS(
    IN p_ID_USUARIO INT
)
BEGIN
    DECLARE v_ID_CITA INT;
    DECLARE v_ID_VACUNA INT;
    DECLARE v_TIPO VARCHAR(20);
    DECLARE v_FECHA_EVENTO DATETIME;
    DECLARE v_MENSAJE_BASE VARCHAR(255);
    DECLARE v_MENSAJE_FINAL VARCHAR(500);
    DECLARE v_NOMBRE_CLIENTE VARCHAR(100);
    DECLARE v_NOMBRE_MASCOTA VARCHAR(50);
    DECLARE v_CEDULA_CLIENTE VARCHAR(10);
    DECLARE v_NOMBRE_SERVICIO VARCHAR(50);
    DECLARE v_VETERINARIOS VARCHAR(255);
    DECLARE v_DONE INT DEFAULT FALSE;
    
    DECLARE v_cursor CURSOR FOR
        SELECT 'CITA' AS TIPO, C.ID_CITA AS ID_EVENTO, C.FECHA_HORA, RC.ANTICIPACION, RC.MENSAJE,
               CONCAT(CL.NOMBRE, ' ', CL.APELLIDO), M.NOMBRE, CL.CEDULA, S.NOMBRE_SERVICIO
        FROM CITA C
        INNER JOIN CLIENTE CL ON C.ID_CLIENTE = CL.ID_CLIENTE
        INNER JOIN MASCOTA M ON C.ID_MASCOTA = M.ID_MASCOTA
        INNER JOIN SERVICIO S ON C.ID_SERVICIO = S.ID_SERVICIO
        CROSS JOIN RECORDATORIO_CONFIG RC
        WHERE C.ESTADO = 'PENDIENTE' AND RC.TIPO_RECORDATORIO = 'CITA' AND RC.ACTIVO = 1
          AND NOT EXISTS (
              SELECT 1 FROM RECORDATORIO_ENVIADO RE
              WHERE RE.ID_CITA = C.ID_CITA AND RE.TIPO = 'CITA'
                AND DATE(RE.FECHA_ENVIO) = DATE(NOW())
          )
          AND (
              (RC.ANTICIPACION = '15_DIAS' AND DATEDIFF(C.FECHA_HORA, NOW()) = 15) OR
              (RC.ANTICIPACION = '7_DIAS' AND DATEDIFF(C.FECHA_HORA, NOW()) = 7) OR
              (RC.ANTICIPACION = '1_MES' AND PERIOD_DIFF(DATE_FORMAT(C.FECHA_HORA, '%Y%m'), DATE_FORMAT(NOW(), '%Y%m')) = 1) OR
              (RC.ANTICIPACION = '12_HORAS' AND TIMESTAMPDIFF(HOUR, NOW(), C.FECHA_HORA) = 12) OR
              (RC.ANTICIPACION = '10_HORAS' AND TIMESTAMPDIFF(HOUR, NOW(), C.FECHA_HORA) = 10) OR
              (RC.ANTICIPACION = '5_MINUTOS' AND TIMESTAMPDIFF(MINUTE, NOW(), C.FECHA_HORA) = 5) OR
              (RC.ANTICIPACION = '30_SEGUNDOS' AND TIMESTAMPDIFF(SECOND, NOW(), C.FECHA_HORA) = 30)
          )
        UNION ALL
        SELECT 'VACUNA', VA.ID_VACUNA, VA.FECHA_PROXIMA, RC.ANTICIPACION, RC.MENSAJE,
               CONCAT(CL.NOMBRE, ' ', CL.APELLIDO), M.NOMBRE, CL.CEDULA, VA.NOMBRE
        FROM VACUNA_APLICADA VA
        INNER JOIN MASCOTA M ON VA.ID_MASCOTA = M.ID_MASCOTA
        INNER JOIN CLIENTE CL ON M.ID_CLIENTE = CL.ID_CLIENTE
        CROSS JOIN RECORDATORIO_CONFIG RC
        WHERE RC.TIPO_RECORDATORIO = 'VACUNA' AND RC.ACTIVO = 1
          AND NOT EXISTS (
              SELECT 1 FROM RECORDATORIO_ENVIADO RE
              WHERE RE.ID_VACUNA = VA.ID_VACUNA AND RE.TIPO = 'VACUNA'
                AND RE.MENSAJE LIKE CONCAT(RC.MENSAJE, '%')
                AND DATE(RE.FECHA_ENVIO) = DATE(NOW())
          )
          AND (
              (RC.ANTICIPACION = '15_DIAS' AND DATEDIFF(VA.FECHA_PROXIMA, NOW()) = 15) OR
              (RC.ANTICIPACION = '7_DIAS' AND DATEDIFF(VA.FECHA_PROXIMA, NOW()) = 7) OR
              (RC.ANTICIPACION = '1_MES' AND PERIOD_DIFF(DATE_FORMAT(VA.FECHA_PROXIMA, '%Y%m'), DATE_FORMAT(NOW(), '%Y%m')) = 1) OR
              (RC.ANTICIPACION = '12_HORAS' AND TIMESTAMPDIFF(HOUR, NOW(), VA.FECHA_PROXIMA) = 12) OR
              (RC.ANTICIPACION = '10_HORAS' AND TIMESTAMPDIFF(HOUR, NOW(), VA.FECHA_PROXIMA) = 10) OR
              (RC.ANTICIPACION = '5_MINUTOS' AND TIMESTAMPDIFF(MINUTE, NOW(), VA.FECHA_PROXIMA) = 5) OR
              (RC.ANTICIPACION = '30_SEGUNDOS' AND TIMESTAMPDIFF(SECOND, NOW(), VA.FECHA_PROXIMA) = 30)
          );
    
    DECLARE CONTINUE HANDLER FOR NOT FOUND SET v_DONE = TRUE;
    
    OPEN v_cursor;
    
    read_loop: LOOP
        FETCH v_cursor INTO v_TIPO, v_ID_CITA, v_FECHA_EVENTO, v_MENSAJE_BASE, v_MENSAJE_FINAL,
                           v_NOMBRE_CLIENTE, v_NOMBRE_MASCOTA, v_CEDULA_CLIENTE, v_NOMBRE_SERVICIO;
        IF v_DONE THEN
            LEAVE read_loop;
        END IF;
        
        IF v_TIPO = 'CITA' THEN
            SELECT CONCAT(V.NOMBRE, ' ', V.APELLIDO) INTO v_VETERINARIOS
            FROM CITA C
            INNER JOIN VETERINARIO V ON C.ID_VETERINARIO = V.ID_VETERINARIO
            WHERE C.ID_CITA = v_ID_CITA;
            
            SET v_MENSAJE_FINAL = CONCAT(v_MENSAJE_BASE, '. Detalles: ',
                DATE_FORMAT(v_FECHA_EVENTO, '%d/%m/%Y %H:%i'),
                ' - Servicio: ', v_NOMBRE_SERVICIO,
                '. Mascota: ', v_NOMBRE_MASCOTA,
                '. Veterinario: ', IFNULL(v_VETERINARIOS, 'Por asignar'));
        ELSE
            SET v_MENSAJE_FINAL = CONCAT(v_MENSAJE_BASE, '. Detalles: ',
                'Vacuna: ', v_NOMBRE_SERVICIO,
                ' - Proxima aplicacion: ', DATE_FORMAT(v_FECHA_EVENTO, '%d/%m/%Y'),
                ' - Mascota: ', v_NOMBRE_MASCOTA,
                ' - Cliente: ', v_NOMBRE_CLIENTE, ' (', v_CEDULA_CLIENTE, ')');
        END IF;
        
        INSERT INTO RECORDATORIO_ENVIADO (ID_USUARIO, ID_CITA, ID_VACUNA, TIPO, MENSAJE)
        VALUES (p_ID_USUARIO, IF(v_TIPO = 'CITA', v_ID_CITA, NULL), IF(v_TIPO = 'VACUNA', v_ID_CITA, NULL), v_TIPO, v_MENSAJE_FINAL);
    END LOOP;
    
    CLOSE v_cursor;
    
    IF p_ID_USUARIO IS NOT NULL THEN
        SELECT ID_RECORDATORIO, MENSAJE, FECHA_ENVIO, LEIDO
        FROM RECORDATORIO_ENVIADO
        WHERE ID_USUARIO = p_ID_USUARIO AND LEIDO = 0
        ORDER BY FECHA_ENVIO DESC;
    END IF;
END //

